use crate::fixtures::{
    Fixture, escape_go_string, group_by_category, has_ambiguity_assertions, has_chunk_assertions,
    has_detect_assertions, has_highlights_assertions, has_process_assertions, sanitize_name,
};
use crate::generators::Generator;
use std::fmt::Write as FmtWrite;
use std::path::Path;

pub struct GoGenerator;

impl Generator for GoGenerator {
    fn name(&self) -> &'static str {
        "Go"
    }

    fn generate(&self, fixtures: &[Fixture], output_dir: &Path) -> Result<(), String> {
        let go_dir = output_dir.join("go");

        // Clean stale test files before writing new ones
        let _ = std::fs::remove_dir_all(&go_dir);

        std::fs::create_dir_all(&go_dir).map_err(|e| format!("Failed to create go output dir: {e}"))?;

        write_go_mod(&go_dir)?;
        write_helpers_test_go(&go_dir)?;

        let groups = group_by_category(fixtures);
        for (category, cat_fixtures) in &groups {
            write_test_file(&go_dir, category, cat_fixtures)?;
        }

        // Run gofmt if available
        let _ = std::process::Command::new("gofmt").args(["-w"]).arg(&go_dir).status();

        Ok(())
    }
}

fn write_go_mod(dir: &Path) -> Result<(), String> {
    let content = r#"// Code generated by e2e-generator. DO NOT EDIT.

module e2e_tests

go 1.26

require github.com/kreuzberg-dev/tree-sitter-language-pack/packages/go v0.0.0

replace github.com/kreuzberg-dev/tree-sitter-language-pack/packages/go => ../../packages/go/v1
"#;
    std::fs::write(dir.join("go.mod"), content).map_err(|e| format!("Failed to write go.mod: {e}"))
}

fn write_helpers_test_go(dir: &Path) -> Result<(), String> {
    let content = r#"// Code generated by e2e-generator. DO NOT EDIT.

package e2e_tests

import (
	"testing"

	tspack "github.com/kreuzberg-dev/tree-sitter-language-pack/packages/go"
)

// newTestRegistry creates a registry for testing and registers cleanup.
func newTestRegistry(t *testing.T) *tspack.Registry {
	t.Helper()

	reg, err := tspack.NewRegistry()
	if err != nil {
		t.Fatalf("Failed to create registry: %v", err)
	}
	t.Cleanup(func() { reg.Close() })

	return reg
}

// skipIfLanguageUnavailable skips the test if the given language is not available.
func skipIfLanguageUnavailable(t *testing.T, reg *tspack.Registry, lang string) {
	t.Helper()

	if !reg.HasLanguage(lang) {
		t.Skipf("Language %q not available", lang)
	}
}
"#;
    std::fs::write(dir.join("helpers_test.go"), content).map_err(|e| format!("Failed to write helpers_test.go: {e}"))
}

fn write_test_file(dir: &Path, category: &str, fixtures: &[&Fixture]) -> Result<(), String> {
    let needs_strings = fixtures.iter().any(|f| {
        f.assertions
            .as_ref()
            .is_some_and(|a| a.process_structure_name_contains.is_some() || a.process_imports_contains_source.is_some())
    });
    let needs_tspack = fixtures
        .iter()
        .any(|f| has_process_assertions(f) || f.language.is_some());

    let mut out = String::new();

    writeln!(out, "// Code generated by e2e-generator. DO NOT EDIT.").unwrap();
    writeln!(out).unwrap();
    writeln!(out, "package e2e_tests").unwrap();
    writeln!(out).unwrap();
    if needs_tspack || needs_strings {
        writeln!(out, "import (").unwrap();
        if needs_strings {
            writeln!(out, "\t\"strings\"").unwrap();
        }
        writeln!(out, "\t\"testing\"").unwrap();
        writeln!(out).unwrap();
        writeln!(
            out,
            "\ttspack \"github.com/kreuzberg-dev/tree-sitter-language-pack/packages/go\""
        )
        .unwrap();
        writeln!(out, ")").unwrap();
    } else {
        writeln!(out, "import \"testing\"").unwrap();
    }
    writeln!(out).unwrap();

    for fixture in fixtures {
        let fn_name = to_go_test_name(&fixture.id);
        let assertions = fixture.assertions.as_ref();

        writeln!(out, "func {fn_name}(t *testing.T) {{").unwrap();
        writeln!(out, "\t// {}", fixture.description).unwrap();
        writeln!(out, "\treg := newTestRegistry(t)").unwrap();

        // Auto-skip: guard any test that requires a specific language.
        let is_availability_agnostic = assertions.is_some_and(|a| {
            a.expect_error == Some(true)
                || a.language_available.is_some()
                || a.languages_not_empty == Some(true)
                || a.highlights_query_is_none == Some(true)
        }) || has_detect_assertions(fixture)
            || has_ambiguity_assertions(fixture);
        let skip_lang = fixture.skip.as_ref().and_then(|s| s.requires_language.as_deref()).or({
            if !is_availability_agnostic {
                fixture.language.as_deref()
            } else {
                None
            }
        });
        if let Some(req_lang) = skip_lang {
            writeln!(
                out,
                "\tskipIfLanguageUnavailable(t, reg, \"{}\")",
                escape_go_string(req_lang)
            )
            .unwrap();
        }

        if assertions.is_some_and(|a| a.expect_error == Some(true)) {
            if let Some(lang) = &fixture.language {
                writeln!(out, "\t_, err := reg.GetLanguage(\"{}\")", escape_go_string(lang)).unwrap();
                writeln!(out, "\tif err == nil {{").unwrap();
                writeln!(
                    out,
                    "\t\tt.Fatalf(\"Expected error loading language %q, got nil\", \"{}\")",
                    escape_go_string(lang)
                )
                .unwrap();
                writeln!(out, "\t}}").unwrap();
            }
        } else if assertions.is_some_and(|a| a.languages_not_empty == Some(true)) {
            writeln!(out, "\tlangs := reg.AvailableLanguages()").unwrap();
            writeln!(out, "\tif len(langs) == 0 {{").unwrap();
            writeln!(out, "\t\tt.Fatal(\"AvailableLanguages() should not be empty\")").unwrap();
            writeln!(out, "\t}}").unwrap();
        } else if assertions.is_some_and(|a| a.language_available.is_some()) {
            let expected = assertions.unwrap().language_available.unwrap();
            let lang = fixture.language.as_deref().unwrap_or("unknown");
            writeln!(out, "\tgot := reg.HasLanguage(\"{}\")", escape_go_string(lang)).unwrap();
            writeln!(out, "\tif got != {} {{", expected).unwrap();
            writeln!(
                out,
                "\t\tt.Fatalf(\"HasLanguage(%q) = %v, want %v\", \"{}\", got, {})",
                escape_go_string(lang),
                expected
            )
            .unwrap();
            writeln!(out, "\t}}").unwrap();
        } else if has_detect_assertions(fixture) {
            let assertions = assertions.unwrap();
            if let Some(ext) = &assertions.detect_from_extension {
                writeln!(
                    out,
                    "\tresult := reg.DetectLanguageFromExtension(\"{}\")",
                    escape_go_string(ext)
                )
                .unwrap();
            } else if let Some(path) = &assertions.detect_from_path {
                writeln!(
                    out,
                    "\tresult := reg.DetectLanguageFromPath(\"{}\")",
                    escape_go_string(path)
                )
                .unwrap();
            } else if let Some(content) = &assertions.detect_from_content {
                writeln!(
                    out,
                    "\tresult := reg.DetectLanguageFromContent(\"{}\")",
                    escape_go_string(content)
                )
                .unwrap();
            }
            if assertions.detect_result_none == Some(true) {
                writeln!(out, "\tif result != nil {{").unwrap();
                writeln!(out, "\t\tt.Fatalf(\"Expected nil result, got %q\", *result)").unwrap();
                writeln!(out, "\t}}").unwrap();
            } else if let Some(expected) = &assertions.detect_result {
                writeln!(out, "\tif result == nil {{").unwrap();
                writeln!(
                    out,
                    "\t\tt.Fatalf(\"Expected %q, got nil\", \"{}\")",
                    escape_go_string(expected)
                )
                .unwrap();
                writeln!(out, "\t}}").unwrap();
                writeln!(out, "\tif *result != \"{}\" {{", escape_go_string(expected)).unwrap();
                writeln!(
                    out,
                    "\t\tt.Fatalf(\"Expected %q, got %q\", \"{}\", *result)",
                    escape_go_string(expected)
                )
                .unwrap();
                writeln!(out, "\t}}").unwrap();
            }
        } else if has_ambiguity_assertions(fixture) {
            let assertions = assertions.unwrap();
            let ext = assertions.ambiguity_extension.as_deref().unwrap_or("");
            writeln!(
                out,
                "\tassigned, alts := reg.ExtensionAmbiguity(\"{}\")",
                escape_go_string(ext)
            )
            .unwrap();
            if assertions.ambiguity_is_none == Some(true) {
                writeln!(out, "\tif assigned != nil {{").unwrap();
                writeln!(
                    out,
                    "\t\tt.Fatalf(\"Expected no ambiguity, got assigned=%q\", *assigned)"
                )
                .unwrap();
                writeln!(out, "\t}}").unwrap();
            } else {
                writeln!(out, "\tif assigned == nil {{").unwrap();
                writeln!(out, "\t\tt.Fatal(\"Expected ambiguity result, got nil\")").unwrap();
                writeln!(out, "\t}}").unwrap();
                if let Some(expected_assigned) = &assertions.ambiguity_assigned {
                    writeln!(out, "\tif *assigned != \"{}\" {{", escape_go_string(expected_assigned)).unwrap();
                    writeln!(
                        out,
                        "\t\tt.Fatalf(\"Expected assigned %q, got %q\", \"{}\", *assigned)",
                        escape_go_string(expected_assigned)
                    )
                    .unwrap();
                    writeln!(out, "\t}}").unwrap();
                }
                if let Some(alt) = &assertions.ambiguity_alternatives_contain {
                    writeln!(out, "\tfoundAlt := false").unwrap();
                    writeln!(out, "\tfor _, a := range alts {{").unwrap();
                    writeln!(out, "\t\tif a == \"{}\" {{", escape_go_string(alt)).unwrap();
                    writeln!(out, "\t\t\tfoundAlt = true").unwrap();
                    writeln!(out, "\t\t\tbreak").unwrap();
                    writeln!(out, "\t\t}}").unwrap();
                    writeln!(out, "\t}}").unwrap();
                    writeln!(out, "\tif !foundAlt {{").unwrap();
                    writeln!(
                        out,
                        "\t\tt.Fatalf(\"Alternatives should contain '{}', got %v\", alts)",
                        escape_go_string(alt)
                    )
                    .unwrap();
                    writeln!(out, "\t}}").unwrap();
                }
            }
        } else if has_highlights_assertions(fixture) {
            let assertions = assertions.unwrap();
            let lang = fixture.language.as_deref().unwrap_or("unknown");
            writeln!(out, "\tq := reg.GetHighlightsQuery(\"{}\")", escape_go_string(lang)).unwrap();
            if assertions.highlights_query_is_none == Some(true) {
                writeln!(out, "\tif q != nil {{").unwrap();
                writeln!(out, "\t\tt.Fatal(\"Expected no highlights query for language\")").unwrap();
                writeln!(out, "\t}}").unwrap();
            } else if assertions.highlights_query_not_empty == Some(true) {
                writeln!(out, "\tif q == nil {{").unwrap();
                writeln!(out, "\t\tt.Fatal(\"Expected highlights query to be present\")").unwrap();
                writeln!(out, "\t}}").unwrap();
                writeln!(out, "\tif len(*q) == 0 {{").unwrap();
                writeln!(out, "\t\tt.Fatal(\"Highlights query should not be empty\")").unwrap();
                writeln!(out, "\t}}").unwrap();
            }
        } else if has_process_assertions(fixture) {
            let lang = fixture.language.as_deref().unwrap_or("unknown");
            let source = fixture.source_code.as_deref().unwrap_or("");
            let assertions = assertions.unwrap();

            if has_chunk_assertions(fixture) {
                let max_chunk_size = assertions.process_chunk_max_size.unwrap_or(512);
                writeln!(out, "\tchunkSize := {}", max_chunk_size).unwrap();
                writeln!(
                    out,
                    "\tresult, err := reg.Process(\"{}\", tspack.ProcessConfig{{Language: \"{}\", ChunkMaxSize: &chunkSize}})",
                    escape_go_string(source),
                    escape_go_string(lang),
                )
                .unwrap();
            } else {
                writeln!(
                    out,
                    "\tresult, err := reg.Process(\"{}\", tspack.ProcessConfig{{Language: \"{}\", Structure: true, Imports: true, Exports: true, Comments: true, Docstrings: true, Symbols: true, Diagnostics: true}})",
                    escape_go_string(source),
                    escape_go_string(lang)
                )
                .unwrap();
            }
            writeln!(out, "\tif err != nil {{").unwrap();
            writeln!(out, "\t\tt.Fatalf(\"process failed: %v\", err)").unwrap();
            writeln!(out, "\t}}").unwrap();

            if has_chunk_assertions(fixture)
                && let Some(min_chunks) = assertions.process_chunk_count_min
            {
                writeln!(out).unwrap();
                writeln!(out, "\tif len(result.Chunks) < {} {{", min_chunks).unwrap();
                writeln!(
                    out,
                    "\t\tt.Fatalf(\"expected at least {} chunk(s), got %d\", len(result.Chunks))",
                    min_chunks
                )
                .unwrap();
                writeln!(out, "\t}}").unwrap();
            }

            if let Some(expected_lang) = &assertions.process_language {
                writeln!(out).unwrap();
                writeln!(
                    out,
                    "\tif result.Metadata.Language != \"{}\" {{",
                    escape_go_string(expected_lang)
                )
                .unwrap();
                writeln!(
                    out,
                    "\t\tt.Fatalf(\"expected language %q, got %q\", \"{}\", result.Metadata.Language)",
                    escape_go_string(expected_lang)
                )
                .unwrap();
                writeln!(out, "\t}}").unwrap();
            }

            if let Some(min_structures) = assertions.process_structure_count_min {
                writeln!(out).unwrap();
                writeln!(out, "\tif len(result.Metadata.Structure) < {} {{", min_structures).unwrap();
                writeln!(
                    out,
                    "\t\tt.Fatalf(\"expected at least {} structure(s), got %d\", len(result.Metadata.Structure))",
                    min_structures
                )
                .unwrap();
                writeln!(out, "\t}}").unwrap();
            }

            if let Some(expected_kind) = &assertions.process_structure_contains_kind {
                writeln!(out).unwrap();
                writeln!(out, "\tfoundKind := false").unwrap();
                writeln!(out, "\tfor _, s := range result.Metadata.Structure {{").unwrap();
                writeln!(out, "\t\tif s.Kind == \"{}\" {{", escape_go_string(expected_kind)).unwrap();
                writeln!(out, "\t\t\tfoundKind = true").unwrap();
                writeln!(out, "\t\t\tbreak").unwrap();
                writeln!(out, "\t\t}}").unwrap();
                writeln!(out, "\t}}").unwrap();
                writeln!(out, "\tif !foundKind {{").unwrap();
                writeln!(
                    out,
                    "\t\tt.Fatal(\"structure should contain a '{}' kind node\")",
                    escape_go_string(expected_kind)
                )
                .unwrap();
                writeln!(out, "\t}}").unwrap();
            }

            if let Some(name_fragment) = &assertions.process_structure_name_contains {
                writeln!(out).unwrap();
                writeln!(out, "\tfoundName := false").unwrap();
                writeln!(out, "\tfor _, s := range result.Metadata.Structure {{").unwrap();
                writeln!(
                    out,
                    "\t\tif s.Name != nil && strings.Contains(*s.Name, \"{}\") {{",
                    escape_go_string(name_fragment)
                )
                .unwrap();
                writeln!(out, "\t\t\tfoundName = true").unwrap();
                writeln!(out, "\t\t\tbreak").unwrap();
                writeln!(out, "\t\t}}").unwrap();
                writeln!(out, "\t}}").unwrap();
                writeln!(out, "\tif !foundName {{").unwrap();
                writeln!(
                    out,
                    "\t\tt.Fatal(\"structure should contain an item with name containing '{}'\")",
                    escape_go_string(name_fragment)
                )
                .unwrap();
                writeln!(out, "\t}}").unwrap();
            }

            if let Some(min_imports) = assertions.process_imports_count_min {
                writeln!(out).unwrap();
                writeln!(out, "\tif len(result.Metadata.Imports) < {} {{", min_imports).unwrap();
                writeln!(
                    out,
                    "\t\tt.Fatalf(\"expected at least {} import(s), got %d\", len(result.Metadata.Imports))",
                    min_imports
                )
                .unwrap();
                writeln!(out, "\t}}").unwrap();
            }

            if let Some(import_source) = &assertions.process_imports_contains_source {
                writeln!(out).unwrap();
                writeln!(out, "\tfoundImport := false").unwrap();
                writeln!(out, "\tfor _, imp := range result.Metadata.Imports {{").unwrap();
                writeln!(
                    out,
                    "\t\tif strings.Contains(imp.Source, \"{}\") {{",
                    escape_go_string(import_source)
                )
                .unwrap();
                writeln!(out, "\t\t\tfoundImport = true").unwrap();
                writeln!(out, "\t\t\tbreak").unwrap();
                writeln!(out, "\t\t}}").unwrap();
                writeln!(out, "\t}}").unwrap();
                writeln!(out, "\tif !foundImport {{").unwrap();
                writeln!(
                    out,
                    "\t\tt.Fatal(\"imports should contain source '{}'\")",
                    escape_go_string(import_source)
                )
                .unwrap();
                writeln!(out, "\t}}").unwrap();
            }

            if let Some(min_exports) = assertions.process_exports_count_min {
                writeln!(out).unwrap();
                writeln!(out, "\tif len(result.Metadata.Exports) < {} {{", min_exports).unwrap();
                writeln!(
                    out,
                    "\t\tt.Fatalf(\"expected at least {} export(s), got %d\", len(result.Metadata.Exports))",
                    min_exports
                )
                .unwrap();
                writeln!(out, "\t}}").unwrap();
            }

            if let Some(min_comments) = assertions.process_comments_count_min {
                writeln!(out).unwrap();
                writeln!(out, "\tif len(result.Metadata.Comments) < {} {{", min_comments).unwrap();
                writeln!(
                    out,
                    "\t\tt.Fatalf(\"expected at least {} comment(s), got %d\", len(result.Metadata.Comments))",
                    min_comments
                )
                .unwrap();
                writeln!(out, "\t}}").unwrap();
            }

            if let Some(min_lines) = assertions.process_metrics_total_lines_min {
                writeln!(out).unwrap();
                writeln!(out, "\tif result.Metadata.Metrics.TotalLines < {} {{", min_lines).unwrap();
                writeln!(
                    out,
                    "\t\tt.Fatalf(\"expected at least {} total line(s), got %d\", result.Metadata.Metrics.TotalLines)",
                    min_lines
                )
                .unwrap();
                writeln!(out, "\t}}").unwrap();
            }

            if let Some(min_code_lines) = assertions.process_metrics_code_lines_min {
                writeln!(out).unwrap();
                writeln!(out, "\tif result.Metadata.Metrics.CodeLines < {} {{", min_code_lines).unwrap();
                writeln!(
                    out,
                    "\t\tt.Fatalf(\"expected at least {} code line(s), got %d\", result.Metadata.Metrics.CodeLines)",
                    min_code_lines
                )
                .unwrap();
                writeln!(out, "\t}}").unwrap();
            }

            if let Some(min_comment_lines) = assertions.process_metrics_comment_lines_min {
                writeln!(out).unwrap();
                writeln!(
                    out,
                    "\tif result.Metadata.Metrics.CommentLines < {} {{",
                    min_comment_lines
                )
                .unwrap();
                writeln!(
                    out,
                    "\t\tt.Fatalf(\"expected at least {} comment line(s), got %d\", result.Metadata.Metrics.CommentLines)",
                    min_comment_lines
                )
                .unwrap();
                writeln!(out, "\t}}").unwrap();
            }

            if let Some(min_depth) = assertions.process_metrics_max_depth_min {
                writeln!(out).unwrap();
                writeln!(out, "\tif result.Metadata.Metrics.MaxDepth < {} {{", min_depth).unwrap();
                writeln!(
                    out,
                    "\t\tt.Fatalf(\"expected max_depth >= {}, got %d\", result.Metadata.Metrics.MaxDepth)",
                    min_depth
                )
                .unwrap();
                writeln!(out, "\t}}").unwrap();
            }

            if let Some(expected_error_count) = assertions.process_metrics_error_count {
                writeln!(out).unwrap();
                writeln!(
                    out,
                    "\tif result.Metadata.Metrics.ErrorCount != {} {{",
                    expected_error_count
                )
                .unwrap();
                writeln!(
                    out,
                    "\t\tt.Fatalf(\"expected error_count {}, got %d\", result.Metadata.Metrics.ErrorCount)",
                    expected_error_count
                )
                .unwrap();
                writeln!(out, "\t}}").unwrap();
            }

            if assertions.process_diagnostics_not_empty == Some(true) {
                writeln!(out).unwrap();
                writeln!(out, "\tif len(result.Metadata.Diagnostics) == 0 {{").unwrap();
                writeln!(out, "\t\tt.Fatal(\"diagnostics should not be empty\")").unwrap();
                writeln!(out, "\t}}").unwrap();
            }
        } else if let Some(lang) = &fixture.language {
            writeln!(out, "\tptr, err := reg.GetLanguage(\"{}\")", escape_go_string(lang)).unwrap();
            writeln!(out, "\tif err != nil {{").unwrap();
            writeln!(
                out,
                "\t\tt.Fatalf(\"Failed to get language %q: %v\", \"{}\", err)",
                escape_go_string(lang)
            )
            .unwrap();
            writeln!(out, "\t}}").unwrap();
            writeln!(out, "\tif ptr == nil {{").unwrap();
            writeln!(
                out,
                "\t\tt.Fatalf(\"Language pointer for %q is nil\", \"{}\")",
                escape_go_string(lang)
            )
            .unwrap();
            writeln!(out, "\t}}").unwrap();
        }

        writeln!(out, "}}").unwrap();
        writeln!(out).unwrap();
    }

    let filename = format!("{}_test.go", sanitize_name(category));
    std::fs::write(dir.join(&filename), &out).map_err(|e| format!("Failed to write {filename}: {e}"))
}

/// Convert a fixture id to a Go test function name (PascalCase with Test prefix).
fn to_go_test_name(id: &str) -> String {
    let sanitized = sanitize_name(id);
    let mut result = String::from("Test");
    let mut capitalize_next = true;

    for c in sanitized.chars() {
        if c == '_' {
            capitalize_next = true;
        } else if capitalize_next {
            result.push(c.to_ascii_uppercase());
            capitalize_next = false;
        } else {
            result.push(c);
        }
    }

    result
}
