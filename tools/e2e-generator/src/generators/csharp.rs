use crate::fixtures::{
    Fixture, escape_csharp_string, group_by_category, has_chunk_assertions, has_detect_assertions,
    has_process_assertions, sanitize_name,
};
use crate::generators::Generator;
use std::fmt::Write as FmtWrite;
use std::path::Path;

pub struct CSharpGenerator;

impl Generator for CSharpGenerator {
    fn name(&self) -> &'static str {
        "C#"
    }

    fn generate(&self, fixtures: &[Fixture], output_dir: &Path) -> Result<(), String> {
        let cs_dir = output_dir.join("csharp");

        // Clean stale test files before writing new ones
        let _ = std::fs::remove_dir_all(&cs_dir);

        std::fs::create_dir_all(&cs_dir).map_err(|e| format!("Failed to create csharp output dir: {e}"))?;

        write_csproj(&cs_dir)?;

        let groups = group_by_category(fixtures);
        for (category, cat_fixtures) in &groups {
            write_test_file(&cs_dir, category, cat_fixtures)?;
        }

        Ok(())
    }
}

fn write_csproj(dir: &Path) -> Result<(), String> {
    let content = r#"<!-- Code generated by e2e-generator. DO NOT EDIT. -->
<Project Sdk="Microsoft.NET.Sdk">

  <PropertyGroup>
    <TargetFramework>net10.0</TargetFramework>
    <ImplicitUsings>enable</ImplicitUsings>
    <Nullable>enable</Nullable>
    <IsPackable>false</IsPackable>
  </PropertyGroup>

  <ItemGroup>
    <PackageReference Include="Microsoft.NET.Test.Sdk" Version="17.12.0" />
    <PackageReference Include="xunit" Version="2.9.3" />
    <PackageReference Include="xunit.runner.visualstudio" Version="3.0.2" />
  </ItemGroup>

  <ItemGroup>
    <ProjectReference Include="../../packages/csharp/TreeSitterLanguagePack/TreeSitterLanguagePack.csproj" />
  </ItemGroup>

</Project>
"#;
    std::fs::write(dir.join("E2ETests.csproj"), content).map_err(|e| format!("Failed to write E2ETests.csproj: {e}"))
}

fn write_test_file(dir: &Path, category: &str, fixtures: &[&Fixture]) -> Result<(), String> {
    let class_name = to_csharp_class_name(category);
    let mut out = String::new();

    writeln!(out, "// Code generated by e2e-generator. DO NOT EDIT.").unwrap();
    writeln!(out).unwrap();
    writeln!(out, "using Xunit;").unwrap();
    writeln!(out, "using TreeSitterLanguagePack;").unwrap();
    writeln!(out).unwrap();
    writeln!(out, "namespace E2E.Tests;").unwrap();
    writeln!(out).unwrap();
    writeln!(out, "public class {class_name}Tests").unwrap();
    writeln!(out, "{{").unwrap();

    for fixture in fixtures {
        let method_name = to_csharp_method_name(&fixture.id);
        let assertions = fixture.assertions.as_ref();

        // Auto-skip: guard any test that requires a specific language.
        let is_availability_agnostic = assertions.is_some_and(|a| {
            a.expect_error == Some(true) || a.language_available.is_some() || a.languages_not_empty == Some(true)
        });
        let skip_lang = fixture.skip.as_ref().and_then(|s| s.requires_language.as_deref()).or({
            if !is_availability_agnostic {
                fixture.language.as_deref()
            } else {
                None
            }
        });

        writeln!(out).unwrap();
        writeln!(out, "    [Fact]").unwrap();
        writeln!(out, "    public void {}()", method_name).unwrap();
        writeln!(out, "    {{").unwrap();
        writeln!(out, "        // {}", fixture.description).unwrap();

        if let Some(lang) = skip_lang {
            writeln!(
                out,
                "        if (!TsPackClient.HasLanguage(\"{}\"))",
                escape_csharp_string(lang)
            )
            .unwrap();
            writeln!(out, "        {{").unwrap();
            writeln!(
                out,
                "            return; // Language '{}' not available",
                escape_csharp_string(lang)
            )
            .unwrap();
            writeln!(out, "        }}").unwrap();
        }

        if assertions.is_some_and(|a| a.expect_error == Some(true)) {
            if let Some(lang) = &fixture.language {
                writeln!(
                    out,
                    "        Assert.ThrowsAny<Exception>(() => TsPackClient.GetLanguage(\"{}\"));",
                    escape_csharp_string(lang)
                )
                .unwrap();
            }
        } else if assertions.is_some_and(|a| a.languages_not_empty == Some(true)) {
            writeln!(out, "        var langs = TsPackClient.AvailableLanguages();").unwrap();
            writeln!(out, "        Assert.NotEmpty(langs);").unwrap();
        } else if assertions.is_some_and(|a| a.language_available.is_some()) {
            let expected = assertions.unwrap().language_available.unwrap();
            let lang = fixture.language.as_deref().unwrap_or("unknown");
            writeln!(
                out,
                "        Assert.{}(TsPackClient.HasLanguage(\"{}\"));",
                if expected { "True" } else { "False" },
                escape_csharp_string(lang)
            )
            .unwrap();
        } else if has_process_assertions(fixture) {
            let lang = fixture.language.as_deref().unwrap_or("unknown");
            let source = fixture.source_code.as_deref().unwrap_or("");
            let assertions = assertions.unwrap();

            if has_chunk_assertions(fixture) {
                let max_chunk_size = assertions.process_chunk_max_size.unwrap_or(512);
                writeln!(
                    out,
                    "        var config = new ProcessConfig {{ Language = \"{}\", ChunkMaxSize = {} }};",
                    escape_csharp_string(lang),
                    max_chunk_size
                )
                .unwrap();
            } else {
                writeln!(
                    out,
                    "        var config = new ProcessConfig {{ Language = \"{}\", Structure = true, Imports = true, Exports = true, Comments = true, Docstrings = true, Symbols = true, Diagnostics = true }};",
                    escape_csharp_string(lang)
                )
                .unwrap();
            }
            writeln!(
                out,
                "        var result = TsPackClient.Process(\"{}\", config);",
                escape_csharp_string(source)
            )
            .unwrap();

            if let Some(expected_lang) = &assertions.process_language {
                writeln!(
                    out,
                    "        Assert.Equal(\"{}\", result.Language);",
                    escape_csharp_string(expected_lang)
                )
                .unwrap();
            }

            if let Some(min_structures) = assertions.process_structure_count_min {
                writeln!(
                    out,
                    "        Assert.True(result.Structure.Count >= {}, \"Should have at least {} structure(s)\");",
                    min_structures, min_structures
                )
                .unwrap();
            }

            if let Some(expected_kind) = &assertions.process_structure_contains_kind {
                writeln!(
                    out,
                    "        Assert.Contains(result.Structure, s => s.Kind.ToString() == \"{}\");",
                    escape_csharp_string(expected_kind)
                )
                .unwrap();
            }

            if let Some(name_fragment) = &assertions.process_structure_name_contains {
                writeln!(
                    out,
                    "        Assert.Contains(result.Structure, s => s.Name != null && s.Name.Contains(\"{}\"));",
                    escape_csharp_string(name_fragment)
                )
                .unwrap();
            }

            if let Some(min_imports) = assertions.process_imports_count_min {
                writeln!(
                    out,
                    "        Assert.True(result.Imports.Count >= {}, \"Should have at least {} import(s)\");",
                    min_imports, min_imports
                )
                .unwrap();
            }

            if let Some(import_source) = &assertions.process_imports_contains_source {
                writeln!(
                    out,
                    "        Assert.Contains(result.Imports, i => i.Source.Contains(\"{}\"));",
                    escape_csharp_string(import_source)
                )
                .unwrap();
            }

            if let Some(min_exports) = assertions.process_exports_count_min {
                writeln!(
                    out,
                    "        Assert.True(result.Exports.Count >= {}, \"Should have at least {} export(s)\");",
                    min_exports, min_exports
                )
                .unwrap();
            }

            if let Some(min_comments) = assertions.process_comments_count_min {
                writeln!(
                    out,
                    "        Assert.True(result.Comments.Count >= {}, \"Should have at least {} comment(s)\");",
                    min_comments, min_comments
                )
                .unwrap();
            }

            if let Some(min_lines) = assertions.process_metrics_total_lines_min {
                writeln!(
                    out,
                    "        Assert.True(result.Metrics.TotalLines >= {}, \"Should have at least {} total line(s)\");",
                    min_lines, min_lines
                )
                .unwrap();
            }

            if let Some(min_code_lines) = assertions.process_metrics_code_lines_min {
                writeln!(
                    out,
                    "        Assert.True(result.Metrics.CodeLines >= {}, \"Should have at least {} code line(s)\");",
                    min_code_lines, min_code_lines
                )
                .unwrap();
            }

            if let Some(min_comment_lines) = assertions.process_metrics_comment_lines_min {
                writeln!(
                    out,
                    "        Assert.True(result.Metrics.CommentLines >= {}, \"Should have at least {} comment line(s)\");",
                    min_comment_lines, min_comment_lines
                )
                .unwrap();
            }

            if let Some(min_depth) = assertions.process_metrics_max_depth_min {
                writeln!(
                    out,
                    "        Assert.True(result.Metrics.MaxDepth >= {}, \"Should have max_depth >= {}\");",
                    min_depth, min_depth
                )
                .unwrap();
            }

            if let Some(expected_error_count) = assertions.process_metrics_error_count {
                writeln!(
                    out,
                    "        Assert.Equal({}, result.Metrics.ErrorCount);",
                    expected_error_count
                )
                .unwrap();
            }

            if assertions.process_diagnostics_not_empty == Some(true) {
                writeln!(out, "        Assert.NotEmpty(result.Diagnostics);").unwrap();
            }

            if has_chunk_assertions(fixture)
                && let Some(min_chunks) = assertions.process_chunk_count_min
            {
                writeln!(
                    out,
                    "        Assert.True(result.Chunks.Count >= {}, \"Should have at least {} chunk(s)\");",
                    min_chunks, min_chunks
                )
                .unwrap();
            }
        } else if has_detect_assertions(fixture) {
            let assertions = assertions.unwrap();

            if let Some(ext) = &assertions.detect_from_extension {
                writeln!(
                    out,
                    "        var detectResult = TsPackClient.DetectLanguage(\"file.{}\");",
                    escape_csharp_string(ext)
                )
                .unwrap();
                if assertions.detect_result_none == Some(true) {
                    writeln!(out, "        Assert.Null(detectResult);").unwrap();
                } else if let Some(expected) = &assertions.detect_result {
                    writeln!(
                        out,
                        "        Assert.Equal(\"{}\", detectResult);",
                        escape_csharp_string(expected)
                    )
                    .unwrap();
                } else {
                    writeln!(out, "        Assert.NotNull(detectResult);").unwrap();
                }
            }

            if let Some(path) = &assertions.detect_from_path {
                writeln!(
                    out,
                    "        var detectResult = TsPackClient.DetectLanguage(\"{}\");",
                    escape_csharp_string(path)
                )
                .unwrap();
                if assertions.detect_result_none == Some(true) {
                    writeln!(out, "        Assert.Null(detectResult);").unwrap();
                } else if let Some(expected) = &assertions.detect_result {
                    writeln!(
                        out,
                        "        Assert.Equal(\"{}\", detectResult);",
                        escape_csharp_string(expected)
                    )
                    .unwrap();
                } else {
                    writeln!(out, "        Assert.NotNull(detectResult);").unwrap();
                }
            }

            if let Some(content) = &assertions.detect_from_content {
                writeln!(
                    out,
                    "        var detectResult = TsPackClient.DetectLanguageFromContent(\"{}\");",
                    escape_csharp_string(content)
                )
                .unwrap();
                if assertions.detect_result_none == Some(true) {
                    writeln!(out, "        Assert.Null(detectResult);").unwrap();
                } else if let Some(expected) = &assertions.detect_result {
                    writeln!(
                        out,
                        "        Assert.Equal(\"{}\", detectResult);",
                        escape_csharp_string(expected)
                    )
                    .unwrap();
                } else {
                    writeln!(out, "        Assert.NotNull(detectResult);").unwrap();
                }
            }

            if let Some(ext) = &assertions.ambiguity_extension {
                writeln!(
                    out,
                    "        var ambiguity = TsPackClient.ExtensionAmbiguity(\"{}\");",
                    escape_csharp_string(ext)
                )
                .unwrap();
                if assertions.ambiguity_is_none == Some(true) {
                    writeln!(out, "        Assert.Null(ambiguity);").unwrap();
                } else {
                    writeln!(out, "        Assert.NotNull(ambiguity);").unwrap();
                    if let Some(assigned) = &assertions.ambiguity_assigned {
                        writeln!(
                            out,
                            "        Assert.Equal(\"{}\", ambiguity!.Assigned);",
                            escape_csharp_string(assigned)
                        )
                        .unwrap();
                    }
                    if let Some(alt) = &assertions.ambiguity_alternatives_contain {
                        writeln!(
                            out,
                            "        Assert.Contains(\"{}\", ambiguity!.Alternatives);",
                            escape_csharp_string(alt)
                        )
                        .unwrap();
                    }
                }
            }

            if assertions.highlights_query_not_empty == Some(true) {
                let lang = fixture.language.as_deref().unwrap_or("unknown");
                writeln!(
                    out,
                    "        var query = TsPackClient.GetHighlightsQuery(\"{}\");",
                    escape_csharp_string(lang)
                )
                .unwrap();
                writeln!(out, "        Assert.NotNull(query);").unwrap();
                writeln!(out, "        Assert.NotEmpty(query!);").unwrap();
            }

            if assertions.highlights_query_is_none == Some(true) {
                let lang = fixture.language.as_deref().unwrap_or("unknown");
                writeln!(
                    out,
                    "        var query = TsPackClient.GetHighlightsQuery(\"{}\");",
                    escape_csharp_string(lang)
                )
                .unwrap();
                writeln!(out, "        Assert.Null(query);").unwrap();
            }
        } else if let Some(lang) = &fixture.language {
            writeln!(
                out,
                "        var langPtr = TsPackClient.GetLanguage(\"{}\");",
                escape_csharp_string(lang)
            )
            .unwrap();
            writeln!(
                out,
                "        Assert.True(langPtr != IntPtr.Zero, \"Language pointer should not be null\");"
            )
            .unwrap();
        }

        writeln!(out, "    }}").unwrap();
    }

    writeln!(out, "}}").unwrap();

    let filename = format!("{class_name}Tests.cs");
    std::fs::write(dir.join(&filename), &out).map_err(|e| format!("Failed to write {filename}: {e}"))
}

/// Convert a category name to a C# PascalCase class name.
fn to_csharp_class_name(category: &str) -> String {
    let sanitized = sanitize_name(category);
    let mut result = String::new();
    let mut capitalize_next = true;

    for c in sanitized.chars() {
        if c == '_' {
            capitalize_next = true;
        } else if capitalize_next {
            result.push(c.to_ascii_uppercase());
            capitalize_next = false;
        } else {
            result.push(c);
        }
    }

    result
}

/// Convert a fixture ID to a C# PascalCase method name.
fn to_csharp_method_name(id: &str) -> String {
    let sanitized = sanitize_name(id);
    let mut result = String::new();
    let mut capitalize_next = true;

    for c in sanitized.chars() {
        if c == '_' {
            capitalize_next = true;
        } else if capitalize_next {
            result.push(c.to_ascii_uppercase());
            capitalize_next = false;
        } else {
            result.push(c);
        }
    }

    result
}
