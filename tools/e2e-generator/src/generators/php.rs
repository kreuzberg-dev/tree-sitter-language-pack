use crate::fixtures::{
    Fixture, escape_php_source, escape_php_string, group_by_category, has_chunk_assertions, has_detect_assertions,
    has_process_assertions, sanitize_name,
};
use crate::generators::Generator;
use std::fmt::Write as FmtWrite;
use std::path::Path;

pub struct PhpGenerator;

impl Generator for PhpGenerator {
    fn name(&self) -> &'static str {
        "PHP"
    }

    fn generate(&self, fixtures: &[Fixture], output_dir: &Path) -> Result<(), String> {
        let php_dir = output_dir.join("php");

        // Clean stale test files before writing new ones
        let _ = std::fs::remove_dir_all(&php_dir);

        std::fs::create_dir_all(php_dir.join("tests")).map_err(|e| format!("Failed to create php output dir: {e}"))?;

        write_composer_json(&php_dir)?;
        write_phpunit_xml(&php_dir)?;

        let groups = group_by_category(fixtures);
        for (category, cat_fixtures) in &groups {
            write_test_file(&php_dir, category, cat_fixtures)?;
        }

        Ok(())
    }
}

fn write_composer_json(dir: &Path) -> Result<(), String> {
    let content = r#"{
    "_comment": "Code generated by e2e-generator. DO NOT EDIT.",
    "name": "kreuzberg/e2e-tests",
    "type": "project",
    "require": {
        "php": ">=8.2"
    },
    "require-dev": {
        "phpunit/phpunit": "^11.0"
    },
    "autoload-dev": {
        "psr-4": {
            "E2E\\Tests\\": "tests/"
        }
    }
}
"#;
    std::fs::write(dir.join("composer.json"), content).map_err(|e| format!("Failed to write composer.json: {e}"))
}

fn write_phpunit_xml(dir: &Path) -> Result<(), String> {
    let content = r#"<?xml version="1.0" encoding="UTF-8"?>
<!-- Code generated by e2e-generator. DO NOT EDIT. -->
<phpunit xmlns:xsi="http://www.w3.org/2001/XMLSchema-instance"
         xsi:noNamespaceSchemaLocation="vendor/phpunit/phpunit/phpunit.xsd"
         bootstrap="vendor/autoload.php"
         colors="true">
    <testsuites>
        <testsuite name="E2E">
            <directory>tests</directory>
        </testsuite>
    </testsuites>
</phpunit>
"#;
    std::fs::write(dir.join("phpunit.xml"), content).map_err(|e| format!("Failed to write phpunit.xml: {e}"))
}

fn write_test_file(dir: &Path, category: &str, fixtures: &[&Fixture]) -> Result<(), String> {
    let class_name = to_php_class_name(category);
    let mut out = String::new();

    writeln!(out, "<?php").unwrap();
    writeln!(out, "// Code generated by e2e-generator. DO NOT EDIT.").unwrap();
    writeln!(out).unwrap();
    writeln!(out, "declare(strict_types=1);").unwrap();
    writeln!(out).unwrap();
    writeln!(out, "namespace E2E\\Tests;").unwrap();
    writeln!(out).unwrap();
    writeln!(out, "use PHPUnit\\Framework\\TestCase;").unwrap();
    writeln!(out).unwrap();
    writeln!(out, "class {class_name}Test extends TestCase").unwrap();
    writeln!(out, "{{").unwrap();

    for fixture in fixtures {
        let method_name = sanitize_name(&fixture.id);
        let assertions = fixture.assertions.as_ref();

        writeln!(out).unwrap();
        writeln!(out, "    public function test_{method_name}(): void").unwrap();
        writeln!(out, "    {{").unwrap();
        writeln!(out, "        // {}", fixture.description).unwrap();

        // Auto-skip: guard any test that requires a specific language.
        let is_availability_agnostic = assertions.is_some_and(|a| {
            a.expect_error == Some(true) || a.language_available.is_some() || a.languages_not_empty == Some(true)
        });
        let skip_lang = fixture.skip.as_ref().and_then(|s| s.requires_language.as_deref()).or({
            if !is_availability_agnostic {
                fixture.language.as_deref()
            } else {
                None
            }
        });
        if let Some(lang) = skip_lang {
            writeln!(
                out,
                "        if (!\\ts_pack_has_language('{}')) {{",
                escape_php_string(lang)
            )
            .unwrap();
            writeln!(
                out,
                "            $this->markTestSkipped('Language \\'{}\\' not available');",
                escape_php_string(lang)
            )
            .unwrap();
            writeln!(out, "        }}").unwrap();
        }

        if assertions.is_some_and(|a| a.expect_error == Some(true)) {
            if let Some(lang) = &fixture.language {
                writeln!(out, "        $this->expectException(\\Exception::class);").unwrap();
                writeln!(out, "        \\ts_pack_get_language('{}');", escape_php_string(lang)).unwrap();
            }
        } else if assertions.is_some_and(|a| a.languages_not_empty == Some(true)) {
            writeln!(out, "        $langs = \\ts_pack_available_languages();").unwrap();
            writeln!(
                out,
                "        $this->assertNotEmpty($langs, 'available_languages() should not be empty');"
            )
            .unwrap();
        } else if assertions.is_some_and(|a| a.language_available.is_some()) {
            let expected = assertions.unwrap().language_available.unwrap();
            let lang = fixture.language.as_deref().unwrap_or("unknown");
            writeln!(
                out,
                "        $this->assert{}(\\ts_pack_has_language('{}'));",
                if expected { "True" } else { "False" },
                escape_php_string(lang)
            )
            .unwrap();
        } else if has_process_assertions(fixture) {
            let lang = fixture.language.as_deref().unwrap_or("unknown");
            let source = fixture.source_code.as_deref().unwrap_or("");
            let assertions = assertions.unwrap();

            if has_chunk_assertions(fixture) {
                let max_chunk_size = assertions.process_chunk_max_size.unwrap_or(512);
                writeln!(
                    out,
                    "        $intel = json_decode(\\ts_pack_process(\"{}\", json_encode(['language' => '{}', 'chunk_max_size' => {}])), true);",
                    escape_php_source(source),
                    escape_php_string(lang),
                    max_chunk_size
                )
                .unwrap();
            } else {
                writeln!(
                    out,
                    "        $intel = json_decode(\\ts_pack_process(\"{}\", json_encode(['language' => '{}', 'structure' => true, 'imports' => true, 'exports' => true, 'comments' => true, 'docstrings' => true, 'symbols' => true, 'diagnostics' => true])), true);",
                    escape_php_source(source),
                    escape_php_string(lang)
                )
                .unwrap();
            }

            if let Some(expected_lang) = &assertions.process_language {
                writeln!(
                    out,
                    "        $this->assertSame('{}', $intel['language']);",
                    escape_php_string(expected_lang)
                )
                .unwrap();
            }

            if let Some(min_structures) = assertions.process_structure_count_min {
                writeln!(
                    out,
                    "        $this->assertGreaterThanOrEqual({}, count($intel['structure'] ?? []), 'Should have at least {} structure(s)');",
                    min_structures, min_structures
                )
                .unwrap();
            }

            if let Some(expected_kind) = &assertions.process_structure_contains_kind {
                writeln!(out, "        $foundKind = false;").unwrap();
                writeln!(out, "        foreach ($intel['structure'] ?? [] as $s) {{").unwrap();
                writeln!(
                    out,
                    "            if ($s['kind'] === '{}') {{ $foundKind = true; break; }}",
                    escape_php_string(expected_kind)
                )
                .unwrap();
                writeln!(out, "        }}").unwrap();
                writeln!(
                    out,
                    "        $this->assertTrue($foundKind, 'Structure should contain a \\'{}\\' kind node');",
                    escape_php_string(expected_kind)
                )
                .unwrap();
            }

            if let Some(name_fragment) = &assertions.process_structure_name_contains {
                writeln!(out, "        $foundName = false;").unwrap();
                writeln!(out, "        foreach ($intel['structure'] ?? [] as $s) {{").unwrap();
                writeln!(
                    out,
                    "            if (str_contains((string)($s['name'] ?? ''), '{}')) {{ $foundName = true; break; }}",
                    escape_php_string(name_fragment)
                )
                .unwrap();
                writeln!(out, "        }}").unwrap();
                writeln!(
                    out,
                    "        $this->assertTrue($foundName, 'Structure should contain a node with name containing \\'{}\\' ');",
                    escape_php_string(name_fragment)
                )
                .unwrap();
            }

            if let Some(min_imports) = assertions.process_imports_count_min {
                writeln!(
                    out,
                    "        $this->assertGreaterThanOrEqual({}, count($intel['imports'] ?? []), 'Should have at least {} import(s)');",
                    min_imports, min_imports
                )
                .unwrap();
            }

            if let Some(import_source) = &assertions.process_imports_contains_source {
                writeln!(out, "        $foundImport = false;").unwrap();
                writeln!(out, "        foreach ($intel['imports'] ?? [] as $i) {{").unwrap();
                writeln!(
                    out,
                    "            if (str_contains(($i['source'] ?? ''), '{}')) {{ $foundImport = true; break; }}",
                    escape_php_string(import_source)
                )
                .unwrap();
                writeln!(out, "        }}").unwrap();
                writeln!(
                    out,
                    "        $this->assertTrue($foundImport, 'Imports should contain source \\'{}\\' ');",
                    escape_php_string(import_source)
                )
                .unwrap();
            }

            if let Some(min_exports) = assertions.process_exports_count_min {
                writeln!(
                    out,
                    "        $this->assertGreaterThanOrEqual({}, count($intel['exports'] ?? []), 'Should have at least {} export(s)');",
                    min_exports, min_exports
                )
                .unwrap();
            }

            if let Some(min_comments) = assertions.process_comments_count_min {
                writeln!(
                    out,
                    "        $this->assertGreaterThanOrEqual({}, count($intel['comments'] ?? []), 'Should have at least {} comment(s)');",
                    min_comments, min_comments
                )
                .unwrap();
            }

            let needs_metrics = assertions.process_metrics_total_lines_min.is_some()
                || assertions.process_metrics_error_count.is_some()
                || assertions.process_metrics_code_lines_min.is_some()
                || assertions.process_metrics_comment_lines_min.is_some()
                || assertions.process_metrics_max_depth_min.is_some();
            if needs_metrics {
                writeln!(out, "        $metrics = $intel['metrics'] ?? [];").unwrap();
            }

            if let Some(min_lines) = assertions.process_metrics_total_lines_min {
                writeln!(
                    out,
                    "        $this->assertGreaterThanOrEqual({}, $metrics['total_lines'], 'Should have at least {} total line(s)');",
                    min_lines, min_lines
                )
                .unwrap();
            }

            if let Some(min_code_lines) = assertions.process_metrics_code_lines_min {
                writeln!(
                    out,
                    "        $this->assertGreaterThanOrEqual({}, $metrics['code_lines'], 'Should have at least {} code line(s)');",
                    min_code_lines, min_code_lines
                )
                .unwrap();
            }

            if let Some(min_comment_lines) = assertions.process_metrics_comment_lines_min {
                writeln!(
                    out,
                    "        $this->assertGreaterThanOrEqual({}, $metrics['comment_lines'], 'Should have at least {} comment line(s)');",
                    min_comment_lines, min_comment_lines
                )
                .unwrap();
            }

            if let Some(min_depth) = assertions.process_metrics_max_depth_min {
                writeln!(
                    out,
                    "        $this->assertGreaterThanOrEqual({}, $metrics['max_depth'], 'Should have max_depth >= {}');",
                    min_depth, min_depth
                )
                .unwrap();
            }

            if let Some(expected_error_count) = assertions.process_metrics_error_count {
                writeln!(
                    out,
                    "        $this->assertSame({}, $metrics['error_count']);",
                    expected_error_count
                )
                .unwrap();
            }

            if assertions.process_diagnostics_not_empty == Some(true) {
                writeln!(
                    out,
                    "        $this->assertNotEmpty($intel['diagnostics'] ?? [], 'Diagnostics should not be empty');"
                )
                .unwrap();
            }

            if has_chunk_assertions(fixture)
                && let Some(min_chunks) = assertions.process_chunk_count_min
            {
                writeln!(
                    out,
                    "        $this->assertGreaterThanOrEqual({}, count($intel['chunks'] ?? []), 'Should have at least {} chunk(s)');",
                    min_chunks, min_chunks
                )
                .unwrap();
            }
        } else if has_detect_assertions(fixture) {
            let assertions = assertions.unwrap();

            if let Some(ext) = &assertions.detect_from_extension {
                writeln!(
                    out,
                    "        $detectResult = \\TreeSitterLanguagePack::detect_language_from_extension('{}');",
                    escape_php_string(ext)
                )
                .unwrap();
                if assertions.detect_result_none == Some(true) {
                    writeln!(
                        out,
                        "        $this->assertNull($detectResult, 'Expected no language detected for extension');"
                    )
                    .unwrap();
                } else if let Some(expected) = &assertions.detect_result {
                    writeln!(
                        out,
                        "        $this->assertSame('{}', $detectResult, 'Expected language \\'{}\\' detected from extension');",
                        escape_php_string(expected),
                        escape_php_string(expected)
                    )
                    .unwrap();
                } else {
                    writeln!(out, "        $this->assertNotNull($detectResult, 'Expected a language to be detected from extension');").unwrap();
                }
            }

            if let Some(path) = &assertions.detect_from_path {
                writeln!(
                    out,
                    "        $detectResult = \\TreeSitterLanguagePack::detect_language_from_path('{}');",
                    escape_php_string(path)
                )
                .unwrap();
                if assertions.detect_result_none == Some(true) {
                    writeln!(
                        out,
                        "        $this->assertNull($detectResult, 'Expected no language detected for path');"
                    )
                    .unwrap();
                } else if let Some(expected) = &assertions.detect_result {
                    writeln!(
                        out,
                        "        $this->assertSame('{}', $detectResult, 'Expected language \\'{}\\' detected from path');",
                        escape_php_string(expected),
                        escape_php_string(expected)
                    )
                    .unwrap();
                } else {
                    writeln!(
                        out,
                        "        $this->assertNotNull($detectResult, 'Expected a language to be detected from path');"
                    )
                    .unwrap();
                }
            }

            if let Some(content) = &assertions.detect_from_content {
                writeln!(
                    out,
                    "        $detectResult = \\TreeSitterLanguagePack::detect_language_from_content(\"{}\");",
                    escape_php_source(content)
                )
                .unwrap();
                if assertions.detect_result_none == Some(true) {
                    writeln!(
                        out,
                        "        $this->assertNull($detectResult, 'Expected no language detected from content');"
                    )
                    .unwrap();
                } else if let Some(expected) = &assertions.detect_result {
                    writeln!(
                        out,
                        "        $this->assertSame('{}', $detectResult, 'Expected language \\'{}\\' detected from content');",
                        escape_php_string(expected),
                        escape_php_string(expected)
                    )
                    .unwrap();
                } else {
                    writeln!(out, "        $this->assertNotNull($detectResult, 'Expected a language to be detected from content');").unwrap();
                }
            }

            if let Some(ext) = &assertions.ambiguity_extension {
                writeln!(
                    out,
                    "        $ambiguity = \\TreeSitterLanguagePack::extension_ambiguity('{}');",
                    escape_php_string(ext)
                )
                .unwrap();
                if assertions.ambiguity_is_none == Some(true) {
                    writeln!(
                        out,
                        "        $this->assertNull($ambiguity, 'Expected no ambiguity for extension');"
                    )
                    .unwrap();
                } else {
                    writeln!(
                        out,
                        "        $this->assertNotNull($ambiguity, 'Expected ambiguity info for extension');"
                    )
                    .unwrap();
                    if let Some(assigned) = &assertions.ambiguity_assigned {
                        writeln!(
                            out,
                            "        $this->assertSame('{}', $ambiguity['assigned'], 'Expected assigned language \\'{}\\' ');",
                            escape_php_string(assigned),
                            escape_php_string(assigned)
                        )
                        .unwrap();
                    }
                    if let Some(alt) = &assertions.ambiguity_alternatives_contain {
                        writeln!(
                            out,
                            "        $this->assertContains('{}', $ambiguity['alternatives'], 'Alternatives should contain \\'{}\\' ');",
                            escape_php_string(alt),
                            escape_php_string(alt)
                        )
                        .unwrap();
                    }
                }
            }

            if assertions.highlights_query_not_empty == Some(true) {
                let lang = fixture.language.as_deref().unwrap_or("unknown");
                writeln!(
                    out,
                    "        $query = \\TreeSitterLanguagePack::get_highlights_query('{}');",
                    escape_php_string(lang)
                )
                .unwrap();
                writeln!(
                    out,
                    "        $this->assertNotNull($query, 'Highlights query should not be null');"
                )
                .unwrap();
                writeln!(
                    out,
                    "        $this->assertNotEmpty($query, 'Highlights query should not be empty');"
                )
                .unwrap();
            }

            if assertions.highlights_query_is_none == Some(true) {
                let lang = fixture.language.as_deref().unwrap_or("unknown");
                writeln!(
                    out,
                    "        $query = \\TreeSitterLanguagePack::get_highlights_query('{}');",
                    escape_php_string(lang)
                )
                .unwrap();
                writeln!(
                    out,
                    "        $this->assertNull($query, 'Highlights query should be null');"
                )
                .unwrap();
            }
        } else if let Some(lang) = &fixture.language {
            let source = fixture.source_code.as_deref().unwrap_or("");
            writeln!(
                out,
                "        $langPtr = \\ts_pack_get_language('{}');",
                escape_php_string(lang)
            )
            .unwrap();

            if assertions.is_some_and(|a| a.tree_not_null == Some(true)) {
                writeln!(
                    out,
                    "        $this->assertIsInt($langPtr, 'Language pointer should be a valid integer handle');"
                )
                .unwrap();

                // Use ts_pack_parse_string for tree-related assertions
                writeln!(
                    out,
                    "        $sexp = \\ts_pack_parse_string('{}', \"{}\");",
                    escape_php_string(lang),
                    escape_php_source(source)
                )
                .unwrap();
                writeln!(
                    out,
                    "        $this->assertNotEmpty($sexp, 'Parse tree S-expression should not be empty');"
                )
                .unwrap();
            } else {
                writeln!(
                    out,
                    "        $this->assertIsInt($langPtr, 'Language pointer should be a valid integer handle');"
                )
                .unwrap();
            }
        }

        writeln!(out, "    }}").unwrap();
    }

    writeln!(out, "}}").unwrap();

    let filename = format!("{class_name}Test.php");
    std::fs::write(dir.join("tests").join(&filename), &out).map_err(|e| format!("Failed to write {filename}: {e}"))
}

/// Convert a category name to a PHP PascalCase class name.
fn to_php_class_name(category: &str) -> String {
    let sanitized = sanitize_name(category);
    let mut result = String::new();
    let mut capitalize_next = true;

    for c in sanitized.chars() {
        if c == '_' {
            capitalize_next = true;
        } else if capitalize_next {
            result.push(c.to_ascii_uppercase());
            capitalize_next = false;
        } else {
            result.push(c);
        }
    }

    result
}
