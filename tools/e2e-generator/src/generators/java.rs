fn write_helpers_java(pkg_dir: &Path) -> Result<(), String> {
    let content = r#"// Code generated by e2e-generator. DO NOT EDIT.

package io.github.treesitter.languagepack.e2e;

import io.github.treesitter.languagepack.TsPackRegistry;

/**
 * Shared helpers for E2E tests.
 */
final class Helpers {

    private Helpers() {}

    /**
     * Create a new registry, failing the test if creation fails.
     */
    static TsPackRegistry createRegistry() {
        return new TsPackRegistry();
    }
}
"#;
    std::fs::write(pkg_dir.join("Helpers.java"), content).map_err(|e| format!("Failed to write Helpers.java: {e}"))
}

fn write_test_file(pkg_dir: &Path, category: &str, fixtures: &[&Fixture]) -> Result<(), String> {
    let needs_json = fixtures.iter().any(|f| has_process_assertions(f));

    let mut out = String::new();
    let class_name = to_java_class_name(category);

    writeln!(out, "// Code generated by e2e-generator. DO NOT EDIT.").unwrap();
    writeln!(out).unwrap();
    writeln!(out, "package io.github.treesitter.languagepack.e2e;").unwrap();
    writeln!(out).unwrap();
    writeln!(out, "import static org.junit.jupiter.api.Assertions.*;").unwrap();
    writeln!(out).unwrap();
    writeln!(out, "import io.github.treesitter.languagepack.TsPackRegistry;").unwrap();
    writeln!(out, "import org.junit.jupiter.api.Test;").unwrap();
    if needs_json {
        writeln!(out).unwrap();
        writeln!(out, "import com.google.gson.Gson;").unwrap();
        writeln!(out, "import com.google.gson.JsonObject;").unwrap();
        writeln!(out, "import com.google.gson.JsonArray;").unwrap();
    }
    writeln!(out).unwrap();
    writeln!(out, "class {class_name}Test {{").unwrap();
    if needs_json {
        writeln!(out).unwrap();
        writeln!(out, "    private static final Gson GSON = new Gson();").unwrap();
    }
    writeln!(out).unwrap();

    for fixture in fixtures {
        let method_name = sanitize_name(&fixture.id);
        let assertions = fixture.assertions.as_ref();

        writeln!(out, "    @Test").unwrap();
        writeln!(out, "    void {method_name}() {{").unwrap();
        writeln!(out, "        // {}", fixture.description).unwrap();

        // Auto-skip: guard any test that requires a specific language.
        let is_availability_agnostic = assertions.is_some_and(|a| {
            a.expect_error == Some(true) || a.language_available.is_some() || a.languages_not_empty == Some(true)
        });
        let skip_lang = fixture.skip.as_ref().and_then(|s| s.requires_language.as_deref()).or({
            if !is_availability_agnostic {
                fixture.language.as_deref()
            } else {
                None
            }
        });
        if let Some(req_lang) = skip_lang {
            writeln!(out, "        try (var registry = Helpers.createRegistry()) {{").unwrap();
            writeln!(
                    out,
                    "            org.junit.jupiter.api.Assumptions.assumeTrue(registry.hasLanguage(\"{}\"), \"Language '{}' not available\");",
                    escape_java_string(req_lang),
                    escape_java_string(req_lang)
                )
                .unwrap();
            writeln!(out, "        }}").unwrap();
        }

        if assertions.is_some_and(|a| a.expect_error == Some(true)) {
            if let Some(lang) = &fixture.language {
                writeln!(out, "        try (var registry = Helpers.createRegistry()) {{").unwrap();
                writeln!(
                    out,
                    "            assertThrows(Exception.class, () -> registry.getLanguage(\"{}\"));",
                    escape_java_string(lang)
                )
                .unwrap();
                writeln!(out, "        }}").unwrap();
            }
        } else if assertions.is_some_and(|a| a.languages_not_empty == Some(true)) {
            writeln!(out, "        try (var registry = Helpers.createRegistry()) {{").unwrap();
            writeln!(
                out,
                "            org.junit.jupiter.api.Assumptions.assumeTrue(registry.languageCount() > 0, \"No languages loaded (native library may not be available)\");"
            )
            .unwrap();
            writeln!(
                out,
                "            assertFalse(registry.availableLanguages().isEmpty(), \"availableLanguages() should not be empty\");"
            )
            .unwrap();
            writeln!(out, "        }}").unwrap();
        } else if assertions.is_some_and(|a| a.language_available.is_some()) {
            let expected = assertions.unwrap().language_available.unwrap();
            let lang = fixture.language.as_deref().unwrap_or("unknown");
            if expected {
                // When expecting true, guard with assumption that languages are loaded
                writeln!(out, "        try (var registry = Helpers.createRegistry()) {{").unwrap();
                writeln!(
                    out,
                    "            org.junit.jupiter.api.Assumptions.assumeTrue(registry.languageCount() > 0, \"No languages loaded (native library may not be available)\");"
                )
                .unwrap();
                writeln!(
                    out,
                    "            assertEquals({expected}, registry.hasLanguage(\"{}\"));",
                    escape_java_string(lang)
                )
                .unwrap();
                writeln!(out, "        }}").unwrap();
            } else {
                writeln!(out, "        try (var registry = Helpers.createRegistry()) {{").unwrap();
                writeln!(
                    out,
                    "            assertEquals({expected}, registry.hasLanguage(\"{}\"));",
                    escape_java_string(lang)
                )
                .unwrap();
                writeln!(out, "        }}").unwrap();
            }
        } else if has_process_assertions(fixture) {
            let lang = fixture.language.as_deref().unwrap_or("unknown");
            let source = fixture.source_code.as_deref().unwrap_or("");
            let assertions = assertions.unwrap();

            writeln!(out, "        try (var registry = Helpers.createRegistry()) {{").unwrap();

            if has_chunk_assertions(fixture) {
                let max_chunk_size = assertions.process_chunk_max_size.unwrap_or(512);
                writeln!(
                    out,
                    "            String configJson = \"{{\\\"language\\\":\\\"{}\\\",\\\"chunk_max_size\\\":{}}}\";",
                    escape_java_string(lang),
                    max_chunk_size
                )
                .unwrap();
            } else {
                writeln!(
                    out,
                    "            String configJson = \"{{\\\"language\\\":\\\"{}\\\",\\\"structure\\\":true,\\\"imports\\\":true,\\\"exports\\\":true,\\\"comments\\\":true,\\\"docstrings\\\":true,\\\"symbols\\\":true,\\\"diagnostics\\\":true}}\";",
                    escape_java_string(lang)
                )
                .unwrap();
            }
            writeln!(
                out,
                "            String resultJson = registry.process(\"{}\", configJson);",
                escape_java_string(source)
            )
            .unwrap();

            writeln!(
                out,
                "            JsonObject intel = GSON.fromJson(resultJson, JsonObject.class);"
            )
            .unwrap();

            if has_chunk_assertions(fixture)
                && let Some(min_chunks) = assertions.process_chunk_count_min
            {
                writeln!(out, "            JsonArray chunks = intel.getAsJsonArray(\"chunks\");").unwrap();
                writeln!(
                        out,
                        "            assertTrue(chunks.size() >= {}, \"Should have at least {} chunk(s), got \" + chunks.size());",
                        min_chunks,
                        min_chunks
                    )
                    .unwrap();
            }

            if let Some(expected_lang) = &assertions.process_language {
                writeln!(
                    out,
                    "            assertEquals(\"{}\", intel.get(\"language\").getAsString());",
                    escape_java_string(expected_lang)
                )
                .unwrap();
            }

            if let Some(min_structures) = assertions.process_structure_count_min {
                writeln!(
                    out,
                    "            assertTrue(intel.getAsJsonArray(\"structure\").size() >= {}, \"Should have at least {} structure(s)\");",
                    min_structures,
                    min_structures
                )
                .unwrap();
            }

            if let Some(expected_kind) = &assertions.process_structure_contains_kind {
                writeln!(out, "            boolean foundKind = false;").unwrap();
                writeln!(
                    out,
                    "            for (var elem : intel.getAsJsonArray(\"structure\")) {{"
                )
                .unwrap();
                writeln!(
                    out,
                    "                if (elem.getAsJsonObject().get(\"kind\").getAsString().equals(\"{}\")) {{",
                    escape_java_string(expected_kind)
                )
                .unwrap();
                writeln!(out, "                    foundKind = true;").unwrap();
                writeln!(out, "                    break;").unwrap();
                writeln!(out, "                }}").unwrap();
                writeln!(out, "            }}").unwrap();
                writeln!(
                    out,
                    "            assertTrue(foundKind, \"Structure should contain a '{}' kind node\");",
                    escape_java_string(expected_kind)
                )
                .unwrap();
            }

            if let Some(name_fragment) = &assertions.process_structure_name_contains {
                writeln!(out, "            boolean foundName = false;").unwrap();
                writeln!(
                    out,
                    "            for (var elem : intel.getAsJsonArray(\"structure\")) {{"
                )
                .unwrap();
                writeln!(
                    out,
                    "                var nameElem = elem.getAsJsonObject().get(\"name\");"
                )
                .unwrap();
                writeln!(
                    out,
                    "                if (nameElem != null && nameElem.getAsString().contains(\"{}\")) {{",
                    escape_java_string(name_fragment)
                )
                .unwrap();
                writeln!(out, "                    foundName = true;").unwrap();
                writeln!(out, "                    break;").unwrap();
                writeln!(out, "                }}").unwrap();
                writeln!(out, "            }}").unwrap();
                writeln!(
                    out,
                    "            assertTrue(foundName, \"Structure should contain an item with name containing '{}'\");",
                    escape_java_string(name_fragment)
                )
                .unwrap();
            }

            if let Some(min_imports) = assertions.process_imports_count_min {
                writeln!(
                    out,
                    "            assertTrue(intel.getAsJsonArray(\"imports\").size() >= {}, \"Should have at least {} import(s)\");",
                    min_imports,
                    min_imports
                )
                .unwrap();
            }

            if let Some(import_source) = &assertions.process_imports_contains_source {
                writeln!(out, "            boolean foundImport = false;").unwrap();
                writeln!(out, "            for (var elem : intel.getAsJsonArray(\"imports\")) {{").unwrap();
                writeln!(
                    out,
                    "                var srcElem = elem.getAsJsonObject().get(\"source\");"
                )
                .unwrap();
                writeln!(
                    out,
                    "                if (srcElem != null && srcElem.getAsString().contains(\"{}\")) {{",
                    escape_java_string(import_source)
                )
                .unwrap();
                writeln!(out, "                    foundImport = true;").unwrap();
                writeln!(out, "                    break;").unwrap();
                writeln!(out, "                }}").unwrap();
                writeln!(out, "            }}").unwrap();
                writeln!(
                    out,
                    "            assertTrue(foundImport, \"Imports should contain source '{}'\");",
                    escape_java_string(import_source)
                )
                .unwrap();
            }

            if let Some(min_exports) = assertions.process_exports_count_min {
                writeln!(
                    out,
                    "            assertTrue(intel.getAsJsonArray(\"exports\").size() >= {}, \"Should have at least {} export(s)\");",
                    min_exports,
                    min_exports
                )
                .unwrap();
            }

            if let Some(min_comments) = assertions.process_comments_count_min {
                writeln!(
                    out,
                    "            assertTrue(intel.getAsJsonArray(\"comments\").size() >= {}, \"Should have at least {} comment(s)\");",
                    min_comments,
                    min_comments
                )
                .unwrap();
            }

            let needs_metrics = assertions.process_metrics_total_lines_min.is_some()
                || assertions.process_metrics_error_count.is_some()
                || assertions.process_metrics_code_lines_min.is_some()
                || assertions.process_metrics_comment_lines_min.is_some()
                || assertions.process_metrics_max_depth_min.is_some();
            if needs_metrics {
                writeln!(
                    out,
                    "            JsonObject metrics = intel.getAsJsonObject(\"metrics\");"
                )
                .unwrap();

                if let Some(min_lines) = assertions.process_metrics_total_lines_min {
                    writeln!(
                        out,
                        "            assertTrue(metrics.get(\"total_lines\").getAsInt() >= {}, \"Should have at least {} total line(s)\");",
                        min_lines,
                        min_lines
                    )
                    .unwrap();
                }

                if let Some(min_code_lines) = assertions.process_metrics_code_lines_min {
                    writeln!(
                        out,
                        "            assertTrue(metrics.get(\"code_lines\").getAsInt() >= {}, \"Should have at least {} code line(s)\");",
                        min_code_lines,
                        min_code_lines
                    )
                    .unwrap();
                }

                if let Some(min_comment_lines) = assertions.process_metrics_comment_lines_min {
                    writeln!(
                        out,
                        "            assertTrue(metrics.get(\"comment_lines\").getAsInt() >= {}, \"Should have at least {} comment line(s)\");",
                        min_comment_lines,
                        min_comment_lines
                    )
                    .unwrap();
                }

                if let Some(min_depth) = assertions.process_metrics_max_depth_min {
                    writeln!(
                        out,
                        "            assertTrue(metrics.get(\"max_depth\").getAsInt() >= {}, \"Should have max_depth >= {}\");",
                        min_depth,
                        min_depth
                    )
                    .unwrap();
                }

                if let Some(expected_error_count) = assertions.process_metrics_error_count {
                    writeln!(
                        out,
                        "            assertEquals({}, metrics.get(\"error_count\").getAsInt(), \"Expected error_count {}\");",
                        expected_error_count,
                        expected_error_count
                    )
                    .unwrap();
                }
            }

            if assertions.process_diagnostics_not_empty == Some(true) {
                writeln!(
                    out,
                    "            assertFalse(intel.getAsJsonArray(\"diagnostics\").isEmpty(), \"Diagnostics should not be empty\");"
                )
                .unwrap();
            }

            writeln!(out, "        }}").unwrap();
        } else if has_detect_assertions(fixture) {
            let assertions = assertions.unwrap();
            writeln!(out, "        try (var registry = Helpers.createRegistry()) {{").unwrap();

            if let Some(ext) = &assertions.detect_from_extension {
                writeln!(
                    out,
                    "            var detectResult = registry.detectLanguageFromExtension(\"{}\");",
                    escape_java_string(ext)
                )
                .unwrap();
                if assertions.detect_result_none == Some(true) {
                    writeln!(
                        out,
                        "            assertNull(detectResult, \"Expected no language detected for extension\");"
                    )
                    .unwrap();
                } else if let Some(expected) = &assertions.detect_result {
                    writeln!(
                        out,
                        "            assertEquals(\"{}\", detectResult, \"Expected language '{}' detected from extension\");",
                        escape_java_string(expected),
                        escape_java_string(expected)
                    )
                    .unwrap();
                } else {
                    writeln!(out, "            assertNotNull(detectResult, \"Expected a language to be detected from extension\");").unwrap();
                }
            }

            if let Some(path) = &assertions.detect_from_path {
                writeln!(
                    out,
                    "            var detectResult = registry.detectLanguageFromPath(\"{}\");",
                    escape_java_string(path)
                )
                .unwrap();
                if assertions.detect_result_none == Some(true) {
                    writeln!(
                        out,
                        "            assertNull(detectResult, \"Expected no language detected for path\");"
                    )
                    .unwrap();
                } else if let Some(expected) = &assertions.detect_result {
                    writeln!(
                        out,
                        "            assertEquals(\"{}\", detectResult, \"Expected language '{}' detected from path\");",
                        escape_java_string(expected),
                        escape_java_string(expected)
                    )
                    .unwrap();
                } else {
                    writeln!(
                        out,
                        "            assertNotNull(detectResult, \"Expected a language to be detected from path\");"
                    )
                    .unwrap();
                }
            }

            if let Some(content) = &assertions.detect_from_content {
                writeln!(
                    out,
                    "            var detectResult = registry.detectLanguageFromContent(\"{}\");",
                    escape_java_string(content)
                )
                .unwrap();
                if assertions.detect_result_none == Some(true) {
                    writeln!(
                        out,
                        "            assertNull(detectResult, \"Expected no language detected from content\");"
                    )
                    .unwrap();
                } else if let Some(expected) = &assertions.detect_result {
                    writeln!(
                        out,
                        "            assertEquals(\"{}\", detectResult, \"Expected language '{}' detected from content\");",
                        escape_java_string(expected),
                        escape_java_string(expected)
                    )
                    .unwrap();
                } else {
                    writeln!(
                        out,
                        "            assertNotNull(detectResult, \"Expected a language to be detected from content\");"
                    )
                    .unwrap();
                }
            }

            if let Some(ext) = &assertions.ambiguity_extension {
                writeln!(
                    out,
                    "            var ambiguity = registry.extensionAmbiguity(\"{}\");",
                    escape_java_string(ext)
                )
                .unwrap();
                if assertions.ambiguity_is_none == Some(true) {
                    writeln!(
                        out,
                        "            assertNull(ambiguity, \"Expected no ambiguity for extension\");"
                    )
                    .unwrap();
                } else {
                    writeln!(
                        out,
                        "            assertNotNull(ambiguity, \"Expected ambiguity info for extension\");"
                    )
                    .unwrap();
                    if let Some(assigned) = &assertions.ambiguity_assigned {
                        writeln!(
                            out,
                            "            assertEquals(\"{}\", ambiguity.getAssigned(), \"Expected assigned language '{}'\");",
                            escape_java_string(assigned),
                            escape_java_string(assigned)
                        )
                        .unwrap();
                    }
                    if let Some(alt) = &assertions.ambiguity_alternatives_contain {
                        writeln!(
                            out,
                            "            assertTrue(ambiguity.getAlternatives().contains(\"{}\"), \"Alternatives should contain '{}'\");",
                            escape_java_string(alt),
                            escape_java_string(alt)
                        )
                        .unwrap();
                    }
                }
            }

            if assertions.highlights_query_not_empty == Some(true) {
                let lang = fixture.language.as_deref().unwrap_or("unknown");
                writeln!(
                    out,
                    "            var query = registry.getHighlightsQuery(\"{}\");",
                    escape_java_string(lang)
                )
                .unwrap();
                writeln!(
                    out,
                    "            assertNotNull(query, \"Highlights query should not be null\");"
                )
                .unwrap();
                writeln!(
                    out,
                    "            assertFalse(query.isEmpty(), \"Highlights query should not be empty\");"
                )
                .unwrap();
            }

            if assertions.highlights_query_is_none == Some(true) {
                let lang = fixture.language.as_deref().unwrap_or("unknown");
                writeln!(
                    out,
                    "            var query = registry.getHighlightsQuery(\"{}\");",
                    escape_java_string(lang)
                )
                .unwrap();
                writeln!(
                    out,
                    "            assertNull(query, \"Highlights query should be null\");"
                )
                .unwrap();
            }

            writeln!(out, "        }}").unwrap();
        } else if let Some(lang) = &fixture.language {
            writeln!(out, "        try (var registry = Helpers.createRegistry()) {{").unwrap();
            writeln!(
                out,
                "            var langPtr = registry.getLanguage(\"{}\");",
                escape_java_string(lang)
            )
            .unwrap();
            writeln!(
                out,
                "            assertNotNull(langPtr, \"Language pointer should not be null\");"
            )
            .unwrap();
            writeln!(out, "        }}").unwrap();
        }

        writeln!(out, "    }}").unwrap();
        writeln!(out).unwrap();
    }

    writeln!(out, "}}").unwrap();

    let filename = format!("{class_name}Test.java");
    std::fs::write(pkg_dir.join(&filename), &out).map_err(|e| format!("Failed to write {filename}: {e}"))
}

/// Convert a category name to a Java PascalCase class name.
fn to_java_class_name(category: &str) -> String {
    let sanitized = sanitize_name(category);
    let mut result = String::new();
    let mut capitalize_next = true;

    for c in sanitized.chars() {
        if c == '_' {
            capitalize_next = true;
        } else if capitalize_next {
            result.push(c.to_ascii_uppercase());
            capitalize_next = false;
        } else {
            result.push(c);
        }
    }

    result
}
