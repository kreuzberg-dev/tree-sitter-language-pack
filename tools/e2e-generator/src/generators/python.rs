use crate::fixtures::{
    Fixture, escape_python_string, group_by_category, has_ambiguity_assertions, has_chunk_assertions,
    has_detect_assertions, has_highlights_assertions, has_process_assertions, sanitize_name,
};
use crate::generators::Generator;
use std::fmt::Write as FmtWrite;
use std::path::Path;

pub struct PythonGenerator;

impl Generator for PythonGenerator {
    fn name(&self) -> &'static str {
        "Python"
    }

    fn generate(&self, fixtures: &[Fixture], output_dir: &Path) -> Result<(), String> {
        let py_dir = output_dir.join("python");
        let _ = std::fs::remove_dir_all(&py_dir);
        std::fs::create_dir_all(py_dir.join("tests"))
            .map_err(|e| format!("Failed to create python output dir: {e}"))?;

        write_pyproject_toml(&py_dir)?;
        write_init_py(&py_dir)?;
        write_helpers_py(&py_dir)?;

        let groups = group_by_category(fixtures);
        for (category, cat_fixtures) in &groups {
            write_test_file(&py_dir, category, cat_fixtures)?;
        }

        // Run ruff format if available
        let _ = std::process::Command::new("ruff")
            .args(["format", "--quiet"])
            .arg(py_dir.join("tests"))
            .status();

        Ok(())
    }
}

fn write_pyproject_toml(dir: &Path) -> Result<(), String> {
    let content = r#"# Code generated by e2e-generator. DO NOT EDIT.

[project]
name = "e2e-tests"
version = "0.0.0"
requires-python = ">=3.10"
dependencies = [
    "tree-sitter-language-pack",
    "pytest",
]

[build-system]
requires = ["setuptools"]
build-backend = "setuptools.build_meta"

[tool.ruff]
line-length = 120

[tool.ruff.lint]
select = ["ALL"]
ignore = [
    "D203",    # incompatible with D211
    "D213",    # incompatible with D212
    "COM812",  # conflicts with formatter
]

[tool.ruff.lint.per-file-ignores]
"tests/*.py" = [
    "S101",     # assert usage expected in tests
    "ANN",      # type annotations not needed for tests
    "D",        # docstring rules relaxed for generated tests
    "INP001",   # implicit namespace package
    "T201",     # print statements
    "PLR2004",  # magic value comparisons expected in tests
    "PT011",    # broad pytest.raises
    "B017",     # broad pytest.raises
    "F841",     # unused variable (root assigned for structure)
    "Q003",     # escaped quotes in generated strings
    "E501",     # line length in generated assertions
]
"#;
    std::fs::write(dir.join("pyproject.toml"), content).map_err(|e| format!("Failed to write pyproject.toml: {e}"))
}

fn write_init_py(dir: &Path) -> Result<(), String> {
    std::fs::write(dir.join("tests").join("__init__.py"), "").map_err(|e| format!("Failed to write __init__.py: {e}"))
}

fn write_helpers_py(dir: &Path) -> Result<(), String> {
    let content = r#"# Code generated by e2e-generator. DO NOT EDIT.


def tree_contains_node_type(node, node_type: str) -> bool:
    """Check whether a tree-sitter node or any descendant has the given type."""
    if node.type == node_type:
        return True
    return any(tree_contains_node_type(child, node_type) for child in node.children)


def tree_has_error_nodes(node) -> bool:
    """Check whether a tree-sitter node or any descendant is an ERROR node."""
    if node.type == "ERROR" or node.is_missing:
        return True
    return any(tree_has_error_nodes(child) for child in node.children)
"#;
    std::fs::write(dir.join("tests").join("helpers.py"), content)
        .map_err(|e| format!("Failed to write helpers.py: {e}"))
}

fn needs_import(fixtures: &[&Fixture], check: fn(&Fixture) -> bool) -> bool {
    fixtures.iter().any(|f| check(f))
}

fn fixture_needs_get_parser(f: &Fixture) -> bool {
    let a = f.assertions.as_ref();
    let is_error = a.is_some_and(|a| a.expect_error == Some(true));
    let is_registry = a.is_some_and(|a| a.languages_not_empty == Some(true));
    let is_lang_check = a.is_some_and(|a| a.language_available.is_some());
    f.language.is_some() && !has_process_assertions(f) && !is_error && !is_registry && !is_lang_check
}

fn fixture_needs_get_language(f: &Fixture) -> bool {
    f.assertions.as_ref().is_some_and(|a| a.expect_error == Some(true))
}

fn fixture_needs_available_languages(f: &Fixture) -> bool {
    f.assertions
        .as_ref()
        .is_some_and(|a| a.languages_not_empty == Some(true))
}

fn fixture_needs_has_language(f: &Fixture) -> bool {
    f.skip.as_ref().is_some_and(|s| s.requires_language.is_some())
        || f.assertions.as_ref().is_some_and(|a| a.language_available.is_some())
}

fn fixture_needs_detect_language(f: &Fixture) -> bool {
    f.assertions
        .as_ref()
        .is_some_and(|a| a.detect_from_extension.is_some() || a.detect_from_path.is_some())
}

fn fixture_needs_detect_from_content(f: &Fixture) -> bool {
    f.assertions.as_ref().is_some_and(|a| a.detect_from_content.is_some())
}

fn fixture_needs_extension_ambiguity(f: &Fixture) -> bool {
    has_ambiguity_assertions(f)
}

fn fixture_needs_get_highlights_query(f: &Fixture) -> bool {
    has_highlights_assertions(f)
}

fn fixture_needs_tree_contains(f: &Fixture) -> bool {
    f.assertions
        .as_ref()
        .is_some_and(|a| a.root_contains_node_type.is_some())
}

fn fixture_needs_tree_has_error(f: &Fixture) -> bool {
    f.assertions.as_ref().is_some_and(|a| a.has_error_nodes == Some(true))
}

fn write_test_file(dir: &Path, category: &str, fixtures: &[&Fixture]) -> Result<(), String> {
    let mut out = String::new();

    // Determine which imports are needed for this category
    let need_get_parser = needs_import(fixtures, fixture_needs_get_parser);
    let need_get_language = needs_import(fixtures, fixture_needs_get_language);
    let need_available_languages = needs_import(fixtures, fixture_needs_available_languages);
    let need_detect_language = needs_import(fixtures, fixture_needs_detect_language);
    let need_detect_from_content = needs_import(fixtures, fixture_needs_detect_from_content);
    let need_extension_ambiguity = needs_import(fixtures, fixture_needs_extension_ambiguity);
    let need_get_highlights_query = needs_import(fixtures, fixture_needs_get_highlights_query);
    let need_has_language = fixtures.iter().any(|f| {
        let is_agnostic = f.assertions.as_ref().is_some_and(|a| {
            a.expect_error == Some(true)
                || a.language_available.is_some()
                || a.languages_not_empty == Some(true)
                || a.highlights_query_is_none == Some(true)
        }) || has_detect_assertions(f)
            || has_ambiguity_assertions(f);
        fixture_needs_has_language(f) || (f.language.is_some() && !is_agnostic)
    });
    let need_process = needs_import(fixtures, has_process_assertions);
    let need_tree_contains = needs_import(fixtures, fixture_needs_tree_contains);
    let need_tree_has_error = needs_import(fixtures, fixture_needs_tree_has_error);
    let need_pytest = fixtures.iter().any(|f| {
        let is_agnostic = f.assertions.as_ref().is_some_and(|a| {
            a.expect_error == Some(true)
                || a.language_available.is_some()
                || a.languages_not_empty == Some(true)
                || a.highlights_query_is_none == Some(true)
        }) || has_detect_assertions(f)
            || has_ambiguity_assertions(f);
        // pytest needed for expect_error tests AND for auto-skip guards
        f.assertions.as_ref().is_some_and(|a| a.expect_error == Some(true))
            || f.skip.as_ref().is_some_and(|s| s.requires_language.is_some())
            || (f.language.is_some() && !is_agnostic)
    });

    writeln!(out, "# Code generated by e2e-generator. DO NOT EDIT.").unwrap();
    writeln!(out).unwrap();

    // Build pack imports list
    let mut pack_imports = Vec::new();
    if need_available_languages {
        pack_imports.push("available_languages");
    }
    if need_detect_language {
        pack_imports.push("detect_language");
    }
    if need_detect_from_content {
        pack_imports.push("detect_language_from_content");
    }
    if need_extension_ambiguity {
        pack_imports.push("extension_ambiguity");
    }
    if need_get_highlights_query {
        pack_imports.push("get_highlights_query");
    }
    if need_get_language {
        pack_imports.push("get_language");
    }
    if need_get_parser {
        pack_imports.push("get_parser");
    }
    if need_has_language {
        pack_imports.push("has_language");
    }
    if need_process {
        pack_imports.push("process");
        pack_imports.push("ProcessConfig");
    }

    // Build helper imports list
    let mut helper_imports = Vec::new();
    if need_tree_contains {
        helper_imports.push("tree_contains_node_type");
    }
    if need_tree_has_error {
        helper_imports.push("tree_has_error_nodes");
    }

    // Emit imports in isort-compatible order:
    // 1. Third-party (pytest, tree_sitter_language_pack)
    // 2. Local (.helpers) — blank line before if third-party present
    let has_third_party = need_pytest || !pack_imports.is_empty();
    if need_pytest {
        writeln!(out, "import pytest").unwrap();
    }
    if !pack_imports.is_empty() {
        if pack_imports.len() == 1 {
            writeln!(out, "from tree_sitter_language_pack import {}", pack_imports[0]).unwrap();
        } else {
            writeln!(out, "from tree_sitter_language_pack import (").unwrap();
            for imp in &pack_imports {
                writeln!(out, "    {imp},").unwrap();
            }
            writeln!(out, ")").unwrap();
        }
    }
    if !helper_imports.is_empty() {
        if has_third_party {
            writeln!(out).unwrap();
        }
        writeln!(out, "from .helpers import {}", helper_imports.join(", ")).unwrap();
    }

    writeln!(out).unwrap();
    writeln!(out).unwrap();

    for fixture in fixtures {
        let fn_name = sanitize_name(&fixture.id);
        let assertions = fixture.assertions.as_ref();

        // Auto-skip: guard any test that requires a specific language.
        let is_availability_agnostic = assertions.is_some_and(|a| {
            a.expect_error == Some(true)
                || a.language_available.is_some()
                || a.languages_not_empty == Some(true)
                || a.highlights_query_is_none == Some(true)
        }) || has_detect_assertions(fixture)
            || has_ambiguity_assertions(fixture);
        let skip_lang = fixture.skip.as_ref().and_then(|s| s.requires_language.as_deref()).or({
            if !is_availability_agnostic {
                fixture.language.as_deref()
            } else {
                None
            }
        });
        if let Some(req_lang) = skip_lang {
            writeln!(
                out,
                "@pytest.mark.skipif(not has_language(\"{req_lang}\"), reason=\"Language '{req_lang}' not available\")"
            )
            .unwrap();
        }

        writeln!(out, "def test_{fn_name}():").unwrap();
        writeln!(out, "    \"\"\"{}\"\"\"", fixture.description).unwrap();

        if assertions.is_some_and(|a| a.expect_error == Some(true)) {
            if let Some(lang) = &fixture.language {
                writeln!(
                    out,
                    "    with pytest.raises(Exception):\n        get_language(\"{}\")",
                    escape_python_string(lang)
                )
                .unwrap();
            }
        } else if assertions.is_some_and(|a| a.languages_not_empty == Some(true)) {
            writeln!(out, "    langs = available_languages()").unwrap();
            writeln!(
                out,
                "    assert len(langs) > 0, \"available_languages() should not be empty\""
            )
            .unwrap();
        } else if assertions.is_some_and(|a| a.language_available.is_some()) {
            let expected = assertions.unwrap().language_available.unwrap();
            let lang = fixture.language.as_deref().unwrap_or("unknown");
            writeln!(
                out,
                "    assert has_language(\"{}\") is {}, \"has_language('{}') should be {}\"",
                escape_python_string(lang),
                if expected { "True" } else { "False" },
                escape_python_string(lang),
                expected
            )
            .unwrap();
        } else if has_detect_assertions(fixture) {
            let assertions = assertions.unwrap();
            if let Some(ext) = &assertions.detect_from_extension {
                writeln!(
                    out,
                    "    result = detect_language(\"file.{}\")",
                    escape_python_string(ext)
                )
                .unwrap();
            } else if let Some(path) = &assertions.detect_from_path {
                writeln!(out, "    result = detect_language(\"{}\")", escape_python_string(path)).unwrap();
            } else if let Some(content) = &assertions.detect_from_content {
                writeln!(
                    out,
                    "    result = detect_language_from_content(\"{}\")",
                    escape_python_string(content)
                )
                .unwrap();
            }
            if assertions.detect_result_none == Some(true) {
                writeln!(out, "    assert result is None, f\"Expected None, got {{result!r}}\"").unwrap();
            } else if let Some(expected) = &assertions.detect_result {
                writeln!(
                    out,
                    "    assert result == \"{}\", f\"Expected '{}', got {{result!r}}\"",
                    escape_python_string(expected),
                    escape_python_string(expected)
                )
                .unwrap();
            }
        } else if has_ambiguity_assertions(fixture) {
            let assertions = assertions.unwrap();
            let ext = assertions.ambiguity_extension.as_deref().unwrap_or("");
            writeln!(
                out,
                "    result = extension_ambiguity(\"{}\")",
                escape_python_string(ext)
            )
            .unwrap();
            if assertions.ambiguity_is_none == Some(true) {
                writeln!(
                    out,
                    "    assert result is None, \"Expected no ambiguity for extension\""
                )
                .unwrap();
            } else {
                writeln!(
                    out,
                    "    assert result is not None, \"Expected ambiguity result for extension\""
                )
                .unwrap();
                writeln!(out, "    assigned, alts = result").unwrap();
                if let Some(assigned) = &assertions.ambiguity_assigned {
                    writeln!(
                        out,
                        "    assert assigned == \"{}\", f\"Expected assigned '{}', got {{assigned!r}}\"",
                        escape_python_string(assigned),
                        escape_python_string(assigned)
                    )
                    .unwrap();
                }
                if let Some(alt) = &assertions.ambiguity_alternatives_contain {
                    writeln!(
                        out,
                        "    assert \"{}\" in alts, f\"Alternatives should contain '{}', got {{alts!r}}\"",
                        escape_python_string(alt),
                        escape_python_string(alt)
                    )
                    .unwrap();
                }
            }
        } else if has_highlights_assertions(fixture) {
            let assertions = assertions.unwrap();
            let lang = fixture.language.as_deref().unwrap_or("unknown");
            writeln!(out, "    q = get_highlights_query(\"{}\")", escape_python_string(lang)).unwrap();
            if assertions.highlights_query_is_none == Some(true) {
                writeln!(
                    out,
                    "    assert q is None, \"Expected no highlights query for language\""
                )
                .unwrap();
            } else if assertions.highlights_query_not_empty == Some(true) {
                writeln!(
                    out,
                    "    assert q is not None, \"Expected highlights query to be present\""
                )
                .unwrap();
                writeln!(out, "    assert len(q) > 0, \"Highlights query should not be empty\"").unwrap();
            }
        } else if has_process_assertions(fixture) {
            let lang = fixture.language.as_deref().unwrap_or("unknown");
            let source = fixture.source_code.as_deref().unwrap_or("");
            let assertions = assertions.unwrap();

            if has_chunk_assertions(fixture) {
                let max_chunk_size = assertions.process_chunk_max_size.unwrap_or(512);
                writeln!(
                    out,
                    "    intel = process(\"{}\", ProcessConfig(language=\"{}\", chunk_max_size={}))",
                    escape_python_string(source),
                    escape_python_string(lang),
                    max_chunk_size
                )
                .unwrap();
                if let Some(min_chunks) = assertions.process_chunk_count_min {
                    writeln!(out, "    chunks = intel.get(\"chunks\", [])").unwrap();
                    writeln!(
                        out,
                        "    assert len(chunks) >= {min_chunks}, f\"Should have at least {min_chunks} chunk(s), got {{len(chunks)}}\""
                    )
                    .unwrap();
                }
            } else {
                writeln!(
                    out,
                    "    intel = process(\"{}\", ProcessConfig(language=\"{}\", structure=True, imports=True, exports=True, comments=True, docstrings=True, symbols=True, diagnostics=True))",
                    escape_python_string(source),
                    escape_python_string(lang)
                )
                .unwrap();
            }

            if let Some(expected_lang) = &assertions.process_language {
                writeln!(
                    out,
                    "    assert intel.get(\"language\") == \"{}\", f\"Expected language '{}', got {{intel.get('language')}}\"",
                    escape_python_string(expected_lang),
                    escape_python_string(expected_lang)
                )
                .unwrap();
            }

            if let Some(min_structures) = assertions.process_structure_count_min {
                writeln!(
                    out,
                    "    assert len(intel.get(\"structure\", [])) >= {min_structures}, \"Should have at least {min_structures} structure(s)\""
                )
                .unwrap();
            }

            if let Some(expected_kind) = &assertions.process_structure_contains_kind {
                writeln!(
                    out,
                    "    assert any(s.get(\"kind\") == \"{}\" for s in intel.get(\"structure\", [])), \"Structure should contain a '{}' kind node\"",
                    escape_python_string(expected_kind),
                    escape_python_string(expected_kind)
                )
                .unwrap();
            }

            if let Some(name_fragment) = &assertions.process_structure_name_contains {
                writeln!(
                    out,
                    "    assert any(\"{}\" in (s.get(\"name\") or \"\") for s in intel.get(\"structure\", [])), \"Structure should contain an item with name containing '{}'\"",
                    escape_python_string(name_fragment),
                    escape_python_string(name_fragment)
                )
                .unwrap();
            }

            if let Some(min_imports) = assertions.process_imports_count_min {
                writeln!(
                    out,
                    "    assert len(intel.get(\"imports\", [])) >= {min_imports}, \"Should have at least {min_imports} import(s)\""
                )
                .unwrap();
            }

            if let Some(import_source) = &assertions.process_imports_contains_source {
                writeln!(
                    out,
                    "    assert any(\"{}\" in (i.get(\"source\") or \"\") for i in intel.get(\"imports\", [])), \"Imports should contain source '{}'\"",
                    escape_python_string(import_source),
                    escape_python_string(import_source)
                )
                .unwrap();
            }

            if let Some(min_exports) = assertions.process_exports_count_min {
                writeln!(
                    out,
                    "    assert len(intel.get(\"exports\", [])) >= {min_exports}, \"Should have at least {min_exports} export(s)\""
                )
                .unwrap();
            }

            if let Some(min_comments) = assertions.process_comments_count_min {
                writeln!(
                    out,
                    "    assert len(intel.get(\"comments\", [])) >= {min_comments}, \"Should have at least {min_comments} comment(s)\""
                )
                .unwrap();
            }

            let needs_metrics = assertions.process_metrics_total_lines_min.is_some()
                || assertions.process_metrics_error_count.is_some()
                || assertions.process_metrics_code_lines_min.is_some()
                || assertions.process_metrics_comment_lines_min.is_some()
                || assertions.process_metrics_max_depth_min.is_some();
            if needs_metrics {
                writeln!(out, "    metrics = intel.get(\"metrics\", {{}})").unwrap();
            }

            if let Some(min_lines) = assertions.process_metrics_total_lines_min {
                writeln!(
                    out,
                    "    assert metrics.get(\"total_lines\", 0) >= {min_lines}, \"Should have at least {min_lines} total line(s)\""
                )
                .unwrap();
            }

            if let Some(min_code_lines) = assertions.process_metrics_code_lines_min {
                writeln!(
                    out,
                    "    assert metrics.get(\"code_lines\", 0) >= {min_code_lines}, \"Should have at least {min_code_lines} code line(s)\""
                )
                .unwrap();
            }

            if let Some(min_comment_lines) = assertions.process_metrics_comment_lines_min {
                writeln!(
                    out,
                    "    assert metrics.get(\"comment_lines\", 0) >= {min_comment_lines}, \"Should have at least {min_comment_lines} comment line(s)\""
                )
                .unwrap();
            }

            if let Some(min_depth) = assertions.process_metrics_max_depth_min {
                writeln!(
                    out,
                    "    assert metrics.get(\"max_depth\", 0) >= {min_depth}, \"Should have max_depth >= {min_depth}\""
                )
                .unwrap();
            }

            if let Some(expected_error_count) = assertions.process_metrics_error_count {
                writeln!(
                    out,
                    "    assert metrics.get(\"error_count\", 0) == {expected_error_count}, f\"Expected error_count {expected_error_count}, got {{metrics.get('error_count', 0)}}\""
                )
                .unwrap();
            }

            if assertions.process_diagnostics_not_empty == Some(true) {
                writeln!(
                    out,
                    "    assert len(intel.get(\"diagnostics\", [])) > 0, \"Diagnostics should not be empty\""
                )
                .unwrap();
            }
        } else if let Some(lang) = &fixture.language {
            let source = fixture.source_code.as_deref().unwrap_or("");
            writeln!(out, "    parser = get_parser(\"{}\")", escape_python_string(lang)).unwrap();
            writeln!(out, "    tree = parser.parse(b\"{}\")", escape_python_string(source)).unwrap();

            if assertions.is_some_and(|a| a.tree_not_null == Some(true)) {
                writeln!(out, "    assert tree is not None, \"Parse tree should not be None\"").unwrap();
                writeln!(out, "    root = tree.root_node").unwrap();

                if let Some(min_children) = assertions.and_then(|a| a.root_child_count_min) {
                    writeln!(
                        out,
                        "    assert root.child_count >= {min_children}, f\"Root should have at least {min_children} child(ren), got {{root.child_count}}\""
                    )
                    .unwrap();
                }

                if let Some(node_type) = assertions.and_then(|a| a.root_contains_node_type.as_deref()) {
                    writeln!(
                        out,
                        "    assert tree_contains_node_type(root, \"{}\"), \"Tree should contain a '{}' node\"",
                        escape_python_string(node_type),
                        escape_python_string(node_type)
                    )
                    .unwrap();
                }

                if assertions.is_some_and(|a| a.has_error_nodes == Some(true)) {
                    writeln!(
                        out,
                        "    assert tree_has_error_nodes(root), \"Tree should contain error nodes\""
                    )
                    .unwrap();
                }
            }
        }

        writeln!(out).unwrap();
        writeln!(out).unwrap();
    }

    // Trim trailing whitespace to single newline
    let trimmed = out.trim_end().to_owned() + "\n";
    let filename = format!("test_{}.py", sanitize_name(category));
    std::fs::write(dir.join("tests").join(&filename), &trimmed).map_err(|e| format!("Failed to write {filename}: {e}"))
}
