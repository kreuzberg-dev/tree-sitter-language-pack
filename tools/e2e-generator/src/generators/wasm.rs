use crate::fixtures::{
    Fixture, escape_js_string, group_by_category, has_chunk_assertions, has_detect_assertions, has_process_assertions,
    sanitize_name,
};
use crate::generators::Generator;
use std::fmt::Write as FmtWrite;
use std::path::Path;

/// Languages available in the WASM build (matches ts-pack-core "wasm" feature group).
const WASM_LANGUAGES: &[&str] = &[
    // Web essentials
    "html",
    "css",
    "javascript",
    "typescript",
    "tsx",
    "json",
    "graphql",
    // Data formats
    "toml",
    "xml",
    "sql",
    "markdown",
    "markdown_inline",
    // Top systems languages
    "c",
    "cpp",
    "rust",
    "go",
    "swift",
    // Top scripting languages
    "python",
    "ruby",
    "bash",
    "php",
    // JVM
    "java",
    "kotlin",
    // Other popular
    "dart",
    "dockerfile",
    "hcl",
    "scss",
    "lua",
    "elixir",
    "scala",
];

pub struct WasmGenerator;

impl Generator for WasmGenerator {
    fn name(&self) -> &'static str {
        "WebAssembly"
    }

    fn generate(&self, fixtures: &[Fixture], output_dir: &Path) -> Result<(), String> {
        let wasm_dir = output_dir.join("wasm");

        // Clean stale test files before writing new ones
        let _ = std::fs::remove_dir_all(&wasm_dir);

        let tests_dir = wasm_dir.join("tests");
        std::fs::create_dir_all(&tests_dir).map_err(|e| format!("Failed to create wasm output dir: {e}"))?;

        write_package_json(&wasm_dir)?;
        write_helpers_ts(&wasm_dir)?;

        // Filter fixtures to only languages available in the WASM web build
        let filtered: Vec<&Fixture> = fixtures
            .iter()
            .filter(|f| {
                // Keep fixtures that test error paths (expect_error) regardless of language
                if f.assertions.as_ref().is_some_and(|a| a.expect_error == Some(true)) {
                    return true;
                }
                // Otherwise filter to web-available languages only
                f.language
                    .as_ref()
                    .map(|lang| WASM_LANGUAGES.contains(&lang.as_str()))
                    .unwrap_or(true)
            })
            .collect();

        let owned: Vec<Fixture> = filtered.into_iter().cloned().collect();
        let groups = group_by_category(&owned);
        for (category, cat_fixtures) in &groups {
            write_test_file(&wasm_dir, category, cat_fixtures)?;
        }

        // Run biome format if available
        let _ = std::process::Command::new("biome")
            .args(["format", "--write"])
            .arg(&wasm_dir)
            .status();

        Ok(())
    }
}

fn write_package_json(dir: &Path) -> Result<(), String> {
    let content = r#"{
  "name": "e2e-wasm-tests",
  "version": "0.0.0",
  "private": true,
  "type": "module",
  "scripts": {
    "test": "vitest run"
  },
  "dependencies": {
    "@kreuzberg/tree-sitter-language-pack-wasm": "file:../../crates/ts-pack-wasm/pkg"
  },
  "devDependencies": {
    "vitest": "^3.0.0"
  }
}
"#;
    std::fs::write(dir.join("package.json"), content).map_err(|e| format!("Failed to write package.json: {e}"))
}

fn write_helpers_ts(dir: &Path) -> Result<(), String> {
    let content = r#"// Code generated by e2e-generator. DO NOT EDIT.

// The nodejs wasm-pack target auto-initializes the WASM module on first require,
// so we simply re-export the functions — no manual init needed.
export {
  availableLanguages,
  hasLanguage,
  languageCount,
  getLanguagePtr,
  parseString,
  treeRootNodeType,
  treeRootChildCount,
  treeContainsNodeType,
  treeHasErrorNodes,
  freeTree,
  process,
  detectLanguageFromExtension,
  detectLanguageFromPath,
  detectLanguageFromContent,
  extensionAmbiguity,
  getHighlightsQuery,
} from "@kreuzberg/tree-sitter-language-pack-wasm";
"#;
    std::fs::write(dir.join("tests").join("helpers.ts"), content)
        .map_err(|e| format!("Failed to write helpers.ts: {e}"))
}

fn write_test_file(dir: &Path, category: &str, fixtures: &[&Fixture]) -> Result<(), String> {
    let mut out = String::new();

    writeln!(out, "// Code generated by e2e-generator. DO NOT EDIT.").unwrap();
    writeln!(out).unwrap();
    writeln!(out, "import {{ describe, it, expect }} from \"vitest\";").unwrap();
    writeln!(
        out,
        "import {{
  availableLanguages,
  hasLanguage,
  getLanguagePtr,
  parseString,
  treeRootNodeType,
  treeRootChildCount,
  treeContainsNodeType,
  treeHasErrorNodes,
  freeTree,
  process,
  detectLanguageFromExtension,
  detectLanguageFromPath,
  detectLanguageFromContent,
  extensionAmbiguity,
  getHighlightsQuery,
}} from \"./helpers\";"
    )
    .unwrap();
    writeln!(out).unwrap();

    let cat_name = sanitize_name(category);
    writeln!(out, "describe(\"{cat_name}\", () => {{").unwrap();

    for fixture in fixtures {
        let test_name = sanitize_name(&fixture.id);
        let assertions = fixture.assertions.as_ref();

        writeln!(out, "  it(\"{test_name}\", () => {{").unwrap();
        writeln!(out, "    // {}", fixture.description).unwrap();

        // Auto-skip: guard any test that requires a specific language.
        let is_availability_agnostic = assertions.is_some_and(|a| {
            a.expect_error == Some(true)
                || a.language_available.is_some()
                || a.languages_not_empty == Some(true)
                || a.detect_from_extension.is_some()
                || a.detect_from_path.is_some()
                || a.detect_from_content.is_some()
                || a.ambiguity_extension.is_some()
                || a.highlights_query_not_empty.is_some()
                || a.highlights_query_is_none.is_some()
        });
        let skip_lang = fixture.skip.as_ref().and_then(|s| s.requires_language.as_deref()).or({
            if !is_availability_agnostic {
                fixture.language.as_deref()
            } else {
                None
            }
        });
        if let Some(req_lang) = skip_lang {
            writeln!(out, "    if (!hasLanguage(\"{req_lang}\")) {{").unwrap();
            writeln!(
                out,
                "      console.log(\"Skipping: language '{}' not available\");",
                req_lang
            )
            .unwrap();
            writeln!(out, "      return;").unwrap();
            writeln!(out, "    }}").unwrap();
        }

        if assertions.is_some_and(|a| a.expect_error == Some(true)) {
            if let Some(lang) = &fixture.language {
                writeln!(
                    out,
                    "    expect(() => getLanguagePtr(\"{}\")).toThrow();",
                    escape_js_string(lang)
                )
                .unwrap();
            }
        } else if assertions.is_some_and(|a| a.languages_not_empty == Some(true)) {
            writeln!(out, "    const langs = availableLanguages();").unwrap();
            writeln!(out, "    expect(langs.length).toBeGreaterThan(0);").unwrap();
        } else if assertions.is_some_and(|a| a.language_available.is_some()) {
            let expected = assertions.unwrap().language_available.unwrap();
            let lang = fixture.language.as_deref().unwrap_or("unknown");
            writeln!(
                out,
                "    expect(hasLanguage(\"{}\")).toBe({});",
                escape_js_string(lang),
                expected
            )
            .unwrap();
        } else if has_process_assertions(fixture) {
            let lang = fixture.language.as_deref().unwrap_or("unknown");
            let source = fixture.source_code.as_deref().unwrap_or("");
            let assertions = assertions.unwrap();

            if has_chunk_assertions(fixture) {
                let max_chunk_size = assertions.process_chunk_max_size.unwrap_or(512);
                writeln!(
                    out,
                    "    const intel = process(\"{}\", {{ language: \"{}\", chunkMaxSize: {} }});",
                    escape_js_string(source),
                    escape_js_string(lang),
                    max_chunk_size
                )
                .unwrap();
                writeln!(out, "    const chunks = intel.chunks || [];").unwrap();
            } else {
                writeln!(
                    out,
                    "    const intel = process(\"{}\", {{ language: \"{}\", structure: true, imports: true, exports: true, comments: true, docstrings: true, symbols: true, diagnostics: true }});",
                    escape_js_string(source),
                    escape_js_string(lang)
                )
                .unwrap();
            }

            if let Some(expected_lang) = &assertions.process_language {
                writeln!(
                    out,
                    "    expect(intel.language).toBe(\"{}\");",
                    escape_js_string(expected_lang)
                )
                .unwrap();
            }

            if let Some(min_structures) = assertions.process_structure_count_min {
                writeln!(
                    out,
                    "    expect((intel.structure || []).length).toBeGreaterThanOrEqual({});",
                    min_structures
                )
                .unwrap();
            }

            if let Some(expected_kind) = &assertions.process_structure_contains_kind {
                writeln!(
                    out,
                    "    expect((intel.structure || []).some((s: any) => s.kind === \"{}\")).toBe(true);",
                    escape_js_string(expected_kind)
                )
                .unwrap();
            }

            if let Some(name_fragment) = &assertions.process_structure_name_contains {
                writeln!(
                    out,
                    "    expect((intel.structure || []).some((s: any) => (s.name || \"\").includes(\"{}\"))).toBe(true);",
                    escape_js_string(name_fragment)
                )
                .unwrap();
            }

            if let Some(min_imports) = assertions.process_imports_count_min {
                writeln!(
                    out,
                    "    expect((intel.imports || []).length).toBeGreaterThanOrEqual({});",
                    min_imports
                )
                .unwrap();
            }

            if let Some(import_source) = &assertions.process_imports_contains_source {
                writeln!(
                    out,
                    "    expect((intel.imports || []).some((i: any) => (i.source || \"\").includes(\"{}\"))).toBe(true);",
                    escape_js_string(import_source)
                )
                .unwrap();
            }

            if let Some(min_exports) = assertions.process_exports_count_min {
                writeln!(
                    out,
                    "    expect((intel.exports || []).length).toBeGreaterThanOrEqual({});",
                    min_exports
                )
                .unwrap();
            }

            if let Some(min_comments) = assertions.process_comments_count_min {
                writeln!(
                    out,
                    "    expect((intel.comments || []).length).toBeGreaterThanOrEqual({});",
                    min_comments
                )
                .unwrap();
            }

            if let Some(min_lines) = assertions.process_metrics_total_lines_min {
                writeln!(
                    out,
                    "    expect((intel.metrics || {{}}).totalLines || 0).toBeGreaterThanOrEqual({});",
                    min_lines
                )
                .unwrap();
            }

            if let Some(min_code_lines) = assertions.process_metrics_code_lines_min {
                writeln!(
                    out,
                    "    expect((intel.metrics || {{}}).codeLines || 0).toBeGreaterThanOrEqual({});",
                    min_code_lines
                )
                .unwrap();
            }

            if let Some(min_comment_lines) = assertions.process_metrics_comment_lines_min {
                writeln!(
                    out,
                    "    expect((intel.metrics || {{}}).commentLines || 0).toBeGreaterThanOrEqual({});",
                    min_comment_lines
                )
                .unwrap();
            }

            if let Some(min_depth) = assertions.process_metrics_max_depth_min {
                writeln!(
                    out,
                    "    expect((intel.metrics || {{}}).maxDepth || 0).toBeGreaterThanOrEqual({});",
                    min_depth
                )
                .unwrap();
            }

            if let Some(expected_error_count) = assertions.process_metrics_error_count {
                writeln!(
                    out,
                    "    expect((intel.metrics || {{}}).errorCount || 0).toBe({});",
                    expected_error_count
                )
                .unwrap();
            }

            if assertions.process_diagnostics_not_empty == Some(true) {
                writeln!(out, "    expect((intel.diagnostics || []).length).toBeGreaterThan(0);").unwrap();
            }

            if has_chunk_assertions(fixture)
                && let Some(min_chunks) = assertions.process_chunk_count_min
            {
                writeln!(
                    out,
                    "    expect((chunks || []).length).toBeGreaterThanOrEqual({});",
                    min_chunks
                )
                .unwrap();
            }
        } else if has_detect_assertions(fixture) {
            let assertions = assertions.unwrap();

            if let Some(ext) = &assertions.detect_from_extension {
                writeln!(
                    out,
                    "    const detectResult = detectLanguageFromExtension(\"{}\");",
                    escape_js_string(ext)
                )
                .unwrap();
                if assertions.detect_result_none == Some(true) {
                    writeln!(out, "    expect(detectResult).toBeUndefined();").unwrap();
                } else if let Some(expected) = &assertions.detect_result {
                    writeln!(
                        out,
                        "    expect(detectResult).toBe(\"{}\");",
                        escape_js_string(expected)
                    )
                    .unwrap();
                } else {
                    writeln!(out, "    expect(detectResult).not.toBeNull();").unwrap();
                }
            }

            if let Some(path) = &assertions.detect_from_path {
                writeln!(
                    out,
                    "    const detectResult = detectLanguageFromPath(\"{}\");",
                    escape_js_string(path)
                )
                .unwrap();
                if assertions.detect_result_none == Some(true) {
                    writeln!(out, "    expect(detectResult).toBeUndefined();").unwrap();
                } else if let Some(expected) = &assertions.detect_result {
                    writeln!(
                        out,
                        "    expect(detectResult).toBe(\"{}\");",
                        escape_js_string(expected)
                    )
                    .unwrap();
                } else {
                    writeln!(out, "    expect(detectResult).not.toBeNull();").unwrap();
                }
            }

            if let Some(content) = &assertions.detect_from_content {
                writeln!(
                    out,
                    "    const detectResult = detectLanguageFromContent(\"{}\");",
                    escape_js_string(content)
                )
                .unwrap();
                if assertions.detect_result_none == Some(true) {
                    writeln!(out, "    expect(detectResult).toBeUndefined();").unwrap();
                } else if let Some(expected) = &assertions.detect_result {
                    writeln!(
                        out,
                        "    expect(detectResult).toBe(\"{}\");",
                        escape_js_string(expected)
                    )
                    .unwrap();
                } else {
                    writeln!(out, "    expect(detectResult).not.toBeNull();").unwrap();
                }
            }

            if let Some(ext) = &assertions.ambiguity_extension {
                writeln!(
                    out,
                    "    const ambiguity = extensionAmbiguity(\"{}\");",
                    escape_js_string(ext)
                )
                .unwrap();
                if assertions.ambiguity_is_none == Some(true) {
                    writeln!(out, "    expect(ambiguity).toBeNull();").unwrap();
                } else {
                    writeln!(out, "    expect(ambiguity).not.toBeNull();").unwrap();
                    if let Some(assigned) = &assertions.ambiguity_assigned {
                        writeln!(
                            out,
                            "    expect(ambiguity?.assigned).toBe(\"{}\");",
                            escape_js_string(assigned)
                        )
                        .unwrap();
                    }
                    if let Some(alt) = &assertions.ambiguity_alternatives_contain {
                        writeln!(
                            out,
                            "    expect(ambiguity?.alternatives ?? []).toContain(\"{}\");",
                            escape_js_string(alt)
                        )
                        .unwrap();
                    }
                }
            }

            if assertions.highlights_query_not_empty == Some(true) {
                let lang = fixture.language.as_deref().unwrap_or("unknown");
                writeln!(
                    out,
                    "    const query = getHighlightsQuery(\"{}\");",
                    escape_js_string(lang)
                )
                .unwrap();
                writeln!(out, "    expect(query).not.toBeNull();").unwrap();
                writeln!(out, "    expect(query?.length ?? 0).toBeGreaterThan(0);").unwrap();
            }

            if assertions.highlights_query_is_none == Some(true) {
                let lang = fixture.language.as_deref().unwrap_or("unknown");
                writeln!(
                    out,
                    "    const query = getHighlightsQuery(\"{}\");",
                    escape_js_string(lang)
                )
                .unwrap();
                writeln!(out, "    expect(query).toBeNull();").unwrap();
            }
        } else if let Some(lang) = &fixture.language {
            // WASM has full parse API — use parseString + tree inspection
            writeln!(
                out,
                "    const tree = parseString(\"{}\", `{}`);",
                escape_js_string(lang),
                escape_js_string(fixture.source_code.as_deref().unwrap_or(""))
            )
            .unwrap();
            writeln!(out, "    expect(tree).toBeTruthy();").unwrap();

            if let Some(a) = assertions {
                if a.tree_not_null == Some(true) {
                    writeln!(out, "    expect(tree).not.toBeNull();").unwrap();
                }
                if let Some(min_count) = a.root_child_count_min {
                    writeln!(
                        out,
                        "    expect(treeRootChildCount(tree)).toBeGreaterThanOrEqual({min_count});"
                    )
                    .unwrap();
                }
                if let Some(node_type) = &a.root_contains_node_type {
                    writeln!(
                        out,
                        "    expect(treeContainsNodeType(tree, \"{}\")).toBe(true);",
                        escape_js_string(node_type)
                    )
                    .unwrap();
                }
                if let Some(has_errors) = a.has_error_nodes {
                    writeln!(out, "    expect(treeHasErrorNodes(tree)).toBe({has_errors});").unwrap();
                }
            }

            writeln!(out, "    freeTree(tree);").unwrap();
        }

        writeln!(out, "  }});").unwrap();
        writeln!(out).unwrap();
    }

    writeln!(out, "}});").unwrap();

    let filename = format!("{}.spec.ts", sanitize_name(category));
    std::fs::write(dir.join("tests").join(&filename), &out).map_err(|e| format!("Failed to write {filename}: {e}"))
}
